 -- Cria o SEQUENCE para o campo [DEV_01_ID] da table [DEV_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_DEV_01_ID'))
  CREATE SEQUENCE dbo.SEQ_DEV_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [DEV_01_ID_OLD] pra receber IDs do campo [DEV_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'DEV_00_01'
 AND COLUMNS.COLUMN_NAME = 'DEV_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.DEV_00_01
 ADD DEV_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [DEV_ID_OLD] pra receber IDs do campo [DEV_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'DEV_00_01'
 AND COLUMNS.COLUMN_NAME = 'DEV_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.DEV_00_01
 ADD DEV_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [DEV_01_ID] de relação com o cabeçalho no novo [DEV_01_ID_OLD] bkp 
 UPDATE DEV_00_01
 SET    DEV_01_ID_OLD = DEV_01_ID
 WHERE  DEV_01_ID_OLD IS NULL 
 GO 
  
 -- Guarda [DEV_ID] de relação com o cabeçalho no novo [DEV_ID_OLD] bkp 
 UPDATE DEV_00_01
 SET    DEV_ID_OLD = DEV_ID
 WHERE  DEV_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_DEV_01_ID pra gerar os novos IDs do campo [DEV_01_ID] 
 UPDATE DEV_00_01
 SET    DEV_01_ID = NEXT VALUE FOR SEQ_DEV_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [DEV_01_ID] para o campo [DEV_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'DEV_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [DEV_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'DEV_00_01_PK')
             ALTER TABLE dbo.DEV_00_01
             DROP CONSTRAINT DEV_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.DEV_00_01
 ADD CONSTRAINT DEV_00_01_pk 
 PRIMARY KEY CLUSTERED (DEV_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [DEV_00_01] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [DEV_00] campo [DEV_ID], na table filha [DEV_00_01] campo [DEV_01_ID]
 UPDATE DEV_00_01 -- FILHA
 SET DEV_00_01.DEV_ID = T2.DEV_ID -- FILHA  RECEBE DA MÃE
 FROM DEV_00_01 -- FILHA
 INNER JOIN DEV_00 T2 ON (DEV_00_01.DEV_ID_OLD = T2.DEV_ID_OLD)
 AND (DEV_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
