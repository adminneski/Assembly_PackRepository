 -- Cria o SEQUENCE para o campo [ETQP_ID] da table [ETQP_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_ETQP_ID'))
  CREATE SEQUENCE dbo.SEQ_ETQP_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [ETQP_ID_OLD] pra receber IDs do campo [ETQP_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'ETQP_00'
 AND COLUMNS.COLUMN_NAME = 'ETQP_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.ETQP_00
 ADD ETQP_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [ETQP_ID] de relação com o cabeçalho no novo [ETQP_ID_OLD] bkp 
 UPDATE ETQP_00
 SET    ETQP_ID_OLD = ETQP_ID
 WHERE  ETQP_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_ETQP_ID pra gerar os novos IDs do campo [ETQP_ID] 
 UPDATE ETQP_00
 SET    ETQP_ID = NEXT VALUE FOR SEQ_ETQP_ID
 GO 
  
-- Recria índice Primary Key da table  [ETQP_ID] para o campo [ETQP_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'ETQP_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [ETQP_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'ETQP_00_pk')
             ALTER TABLE dbo.ETQP_00
             DROP CONSTRAINT ETQP_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.ETQP_00
 ADD CONSTRAINT ETQP_00_PK
 PRIMARY KEY CLUSTERED (ETQP_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON)    
 ON [PRIMARY]

ALTER TABLE dbo.ETQP_00
ADD CONSTRAINT ETQP_00_uq 
UNIQUE NONCLUSTERED (COM_CODIGO, PES_CODIGO ,PRO_CODIGO)
WITH (
  PAD_INDEX = OFF,  
  IGNORE_DUP_KEY = OFF,
  STATISTICS_NORECOMPUTE = OFF,
  ALLOW_ROW_LOCKS = ON,
  ALLOW_PAGE_LOCKS = ON) 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [ETQP_00] 
 REBUILD 
 END
 GO