 -- Cria o SEQUENCE para o campo [CPF_ID] da table [CPF_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CPF_ID'))
  CREATE SEQUENCE dbo.SEQ_CPF_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [CPF_ID_OLD] pra receber IDs do campo [CPF_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CPF_00'
 AND COLUMNS.COLUMN_NAME = 'CPF_ID_OLD')
 BEGIN
 ALTER TABLE dbo.CPF_00
 ADD CPF_ID_OLD int NULL
 END;
 GO

 -- Guarda [CPF_ID] de relação com o cabeçalho no novo [CPF_ID_OLD] bkp 
 UPDATE CPF_00
 SET    CPF_ID_OLD = CPF_ID
 WHERE  CPF_ID_OLD IS NULL 
 GO
 
 -- Executa o SEQ_CPF_ID pra gerar os novos IDs do campo [CPF_ID] 
 UPDATE CPF_00
 SET    CPF_ID = NEXT VALUE FOR SEQ_CPF_ID
 GO 
  
 -- Recria índice Primary Key da table  [CPF_ID] para o campo [CPF_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CPF_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CPF_00]
 DISABLE
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CPF_00_PK')
             ALTER TABLE dbo.CPF_00
             DROP CONSTRAINT CPF_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CPF_00
 ADD CONSTRAINT CPF_00_pk 
 PRIMARY KEY CLUSTERED (CPF_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CPF_00] 
 REBUILD
 END
 GO