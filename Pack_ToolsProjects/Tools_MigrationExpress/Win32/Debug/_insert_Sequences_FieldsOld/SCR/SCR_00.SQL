 -- Cria o SEQUENCE para o campo [SCR_ID] da table [SCR_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_SCR_ID'))
  CREATE SEQUENCE dbo.SEQ_SCR_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [SCR_ID_OLD] pra receber IDs do campo [SCR_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'SCR_00'
 AND COLUMNS.COLUMN_NAME = 'SCR_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.SCR_00
 ADD SCR_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [SCR_ID] de relação com o cabeçalho no novo [SCR_ID_OLD] bkp 
 UPDATE SCR_00
 SET    SCR_ID_OLD = SCR_ID
 WHERE  SCR_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_SCR_ID pra gerar os novos IDs do campo [SCR_ID] 
 UPDATE SCR_00
 SET    SCR_ID = NEXT VALUE FOR SEQ_SCR_ID
 GO 
  
 -- Recria índice Primary Key da table  [SCR_ID] para o campo [SCR_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'SCR_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [SCR_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'PK_SCR_00')
             ALTER TABLE dbo.SCR_00
             DROP CONSTRAINT PK_SCR_00
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.SCR_00
 ADD CONSTRAINT SCR_00_pk 
 PRIMARY KEY CLUSTERED (SCR_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [SCR_00] 
 REBUILD 
 END
 GO
  
