 -- Cria o SEQUENCE para o campo [CPA_01_ID] da table [CPA_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CPA_01_ID'))
  CREATE SEQUENCE dbo.SEQ_CPA_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [CPA_01_ID_OLD] pra receber IDs do campo [CPA_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CPA_00_01'
 AND COLUMNS.COLUMN_NAME = 'CPA_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CPA_00_01
 ADD CPA_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [CPA_ID_OLD] pra receber IDs do campo [CPA_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CPA_00_01'
 AND COLUMNS.COLUMN_NAME = 'CPA_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CPA_00_01
 ADD CPA_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [CPA_01_ID] de relação com o cabeçalho no novo [CPA_01_ID_OLD] bkp 
 UPDATE CPA_00_01
 SET    CPA_01_ID_OLD = CPA_01_ID
 WHERE  CPA_01_ID_OLD IS NULL 
 GO 
  
 -- Criando campo [CPA_INTEGRACAO_OLD] pra receber BKP DAS INTEGRAÇÕES do campo [CPA_INTEGRACAO] 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'CPA_00_01'
             AND   COLUMNS.COLUMN_NAME = 'CPA_INTEGRACAO')
 BEGIN 
    IF NOT EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
                    WHERE COLUMNS.TABLE_NAME = 'CPA_00_01'
                    AND COLUMNS.COLUMN_NAME  = 'CPA_INTEGRACAO_OLD')
    BEGIN 
       ALTER TABLE dbo.CPA_00_01 ADD CPA_INTEGRACAO_OLD VARCHAR(50) NULL 
    END; 
 END; 
 GO 
  
 -- Guarda [CPA_INTEGRACAO] de integrações no novo [CPA_INTEGRACAO_OLD] bkp 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'CPA_00_01'
             AND   COLUMNS.COLUMN_NAME = 'CPA_INTEGRACAO')
 BEGIN 
    UPDATE CPA_00_01
    SET    [CPA_INTEGRACAO_OLD] = CPA_INTEGRACAO
    WHERE  [CPA_INTEGRACAO_OLD] IS NULL 
 END; 
 GO 
  
 -- Guarda [CPA_ID] de relação com o cabeçalho no novo [CPA_ID_OLD] bkp 
 UPDATE CPA_00_01
 SET    CPA_ID_OLD = CPA_ID
 WHERE  CPA_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_CPA_01_ID pra gerar os novos IDs do campo [CPA_01_ID] 
 UPDATE CPA_00_01
 SET    CPA_01_ID = NEXT VALUE FOR SEQ_CPA_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [CPA_01_ID] para o campo [CPA_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CPA_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CPA_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CPA_00_01_PK')
             ALTER TABLE dbo.CPA_00_01
             DROP CONSTRAINT CPA_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CPA_00_01
 ADD CONSTRAINT CPA_00_01_pk 
 PRIMARY KEY CLUSTERED (CPA_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CPA_00_01] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [CPA_00] campo [CPA_ID], na table filha [CPA_00_01] campo [CPA_01_ID]
 UPDATE CPA_00_01 -- FILHA
 SET CPA_00_01.CPA_ID = T2.CPA_ID -- FILHA  RECEBE DA MÃE
 FROM CPA_00_01 -- FILHA
 INNER JOIN CPA_00 T2 ON (CPA_00_01.CPA_ID_OLD = T2.CPA_ID_OLD)
 AND (CPA_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
