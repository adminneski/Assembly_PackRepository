 -- Executa o SEQ_CPA_03_01_ID pra gerar os novos IDs do campo [CPA_03_01_ID] 
 UPDATE CPA_00_03_01
 SET    CPA_03_01_ID = NEXT VALUE FOR SEQ_CPA_03_01_ID
