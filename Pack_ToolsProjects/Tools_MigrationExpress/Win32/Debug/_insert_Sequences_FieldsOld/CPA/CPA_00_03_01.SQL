 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CPA_00_03_01_PK')
             ALTER TABLE dbo.CPA_00_03_01
             DROP CONSTRAINT CPA_00_03_01_pk 
  
