 -- Cria o SEQUENCE para o campo [OMF_ID] da table [OMF_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_OMF_ID'))
  CREATE SEQUENCE dbo.SEQ_OMF_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [OMF_ID_OLD] pra receber IDs do campo [OMF_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'OMF_00'
 AND COLUMNS.COLUMN_NAME = 'OMF_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.OMF_00
 ADD OMF_ID_OLD int NULL 
 END; 
 GO 
  
 -- "INTEGRACÃO" Criando campo [OMF_INTEGRACAO_OLD] pra receber BKP DAS INTEGRAÇÕES do campo [OMF_INTEGRACAO] 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'OMF_00'
             AND   COLUMNS.COLUMN_NAME = 'OMF_INTEGRACAO')
 BEGIN 
    IF NOT EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
                    WHERE COLUMNS.TABLE_NAME = 'OMF_00'
                    AND COLUMNS.COLUMN_NAME  = 'OMF_INTEGRACAO_OLD')
    BEGIN 
       ALTER TABLE dbo.OMF_00 ADD OMF_INTEGRACAO_OLD VARCHAR(50) NULL 
    END; 
 END; 
 GO 
  
 -- "INTEGRACÃO" Guarda [OMF_INTEGRACAO] de integrações no novo [OMF_INTEGRACAO_OLD] bkp 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'OMF_00'
             AND   COLUMNS.COLUMN_NAME = 'OMF_INTEGRACAO')
 BEGIN 
    UPDATE OMF_00
    SET    [OMF_INTEGRACAO_OLD] = OMF_INTEGRACAO
    WHERE  [OMF_INTEGRACAO_OLD] IS NULL 
 END; 
 GO 
  
 -- "1267" Guarda [OMF_ID] de relação com o cabeçalho no novo [OMF_ID_OLD] bkp 
 UPDATE OMF_00
 SET    OMF_ID_OLD = OMF_ID
 WHERE  OMF_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_OMF_ID pra gerar os novos IDs do campo [OMF_ID] 
 UPDATE OMF_00
 SET    OMF_ID = NEXT VALUE FOR SEQ_OMF_ID
 GO 
  
