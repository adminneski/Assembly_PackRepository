 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'OMF_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [OMF_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'OMF_00_PK')
             ALTER TABLE dbo.OMF_00
             DROP CONSTRAINT OMF_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.OMF_00
 ADD CONSTRAINT OMF_00_pk 
 PRIMARY KEY CLUSTERED (OMF_ID) 
 WITH ( 
