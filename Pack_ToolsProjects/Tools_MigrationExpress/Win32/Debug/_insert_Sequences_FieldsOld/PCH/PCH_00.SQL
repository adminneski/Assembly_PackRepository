 -- Cria o SEQUENCE para o campo [PCH_ID] da table [PCH_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_PCH_ID'))
  CREATE SEQUENCE dbo.SEQ_PCH_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [PCH_ID_OLD] pra receber IDs do campo [PCH_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'PCH_00'
 AND COLUMNS.COLUMN_NAME = 'PCH_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.PCH_00
 ADD PCH_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [PCH_ID] de relação com o cabeçalho no novo [PCH_ID_OLD] bkp 
 UPDATE PCH_00
 SET    PCH_ID_OLD = PCH_ID
 WHERE  PCH_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_PCH_ID pra gerar os novos IDs do campo [PCH_ID] 
 UPDATE PCH_00
 SET    PCH_ID = NEXT VALUE FOR SEQ_PCH_ID
 GO 
  
 -- Recria índice Primary Key da table  [PCH_ID] para o campo [PCH_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'PCH_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [PCH_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'PCH_00_PK')
             ALTER TABLE dbo.PCH_00
             DROP CONSTRAINT PCH_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.PCH_00
 ADD CONSTRAINT PCH_00_pk 
 PRIMARY KEY CLUSTERED (PCH_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [PCH_00] 
 REBUILD 
 END
 GO
  
