 -- Cria o SEQUENCE para o campo [MEG_01_ID] da table [MEG_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_MEG_01_ID'))
  CREATE SEQUENCE dbo.SEQ_MEG_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [MEG_01_ID_OLD] pra receber IDs do campo [MEG_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'MEG_00_01'
 AND COLUMNS.COLUMN_NAME = 'MEG_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.MEG_00_01
 ADD MEG_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [MEG_ID_OLD] pra receber IDs do campo [MEG_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'MEG_00_01'
 AND COLUMNS.COLUMN_NAME = 'MEG_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.MEG_00_01
 ADD MEG_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [MEG_01_ID] de relação com o cabeçalho no novo [MEG_01_ID_OLD] bkp 
 UPDATE MEG_00_01
 SET    MEG_01_ID_OLD = MEG_01_ID
 WHERE  MEG_01_ID_OLD IS NULL 
 GO 
  
 -- Guarda [MEG_ID] de relação com o cabeçalho no novo [MEG_ID_OLD] bkp 
 UPDATE MEG_00_01
 SET    MEG_ID_OLD = MEG_ID
 WHERE  MEG_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_MEG_01_ID pra gerar os novos IDs do campo [MEG_01_ID] 
 UPDATE MEG_00_01
 SET    MEG_01_ID = NEXT VALUE FOR SEQ_MEG_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [MEG_01_ID] para o campo [MEG_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'MEG_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [MEG_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'MEG_00_01_PK')
             ALTER TABLE dbo.MEG_00_01
             DROP CONSTRAINT MEG_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.MEG_00_01
 ADD CONSTRAINT MEG_00_01_pk 
 PRIMARY KEY CLUSTERED (MEG_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [MEG_00_01] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [MEG_00] campo [MEG_ID], na table filha [MEG_00_01] campo [MEG_01_ID]
 UPDATE MEG_00_01 -- FILHA
 SET MEG_00_01.MEG_ID = T2.MEG_ID -- FILHA  RECEBE DA MÃE
 FROM MEG_00_01 -- FILHA
 INNER JOIN MEG_00 T2 ON (MEG_00_01.MEG_ID_OLD = T2.MEG_ID_OLD)
 AND (MEG_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
