 -- Cria o SEQUENCE para o campo [PMC_ID] da table [PMC_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_PMC_ID'))
  CREATE SEQUENCE dbo.SEQ_PMC_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [PMC_ID_OLD] pra receber IDs do campo [PMC_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'PMC_00'
 AND COLUMNS.COLUMN_NAME = 'PMC_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.PMC_00
 ADD PMC_ID_OLD int NULL 
 END; 
 GO 
  
 -- "1267" Guarda [PMC_ID] de relação com o cabeçalho no novo [PMC_ID_OLD] bkp 
 UPDATE PMC_00
 SET    PMC_ID_OLD = PMC_ID
 WHERE  PMC_ID_OLD IS NULL 
 GO 

  
 -- Executa o SEQ_PMC_ID pra gerar os novos IDs do campo [PMC_ID] 
 UPDATE PMC_00
 SET    PMC_ID = NEXT VALUE FOR SEQ_PMC_ID
 GO 
  
 -- Recria índice Primary Key da table  [PMC_ID] para o campo [PMC_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'PMC_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [PMC_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'PMC_00_PK')
             ALTER TABLE dbo.PMC_00
             DROP CONSTRAINT PMC_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.PMC_00
 ADD CONSTRAINT PMC_00_pk 
 PRIMARY KEY CLUSTERED (PMC_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [PMC_00] 
 REBUILD 
 END
 GO
  
