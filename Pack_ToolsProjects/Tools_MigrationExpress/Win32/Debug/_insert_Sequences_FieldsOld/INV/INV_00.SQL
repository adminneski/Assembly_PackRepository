 -- Cria o SEQUENCE para o campo [INV_ID] da table [INV_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_INV_ID'))
  CREATE SEQUENCE dbo.SEQ_INV_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [INV_ID_OLD] pra receber IDs do campo [INV_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'INV_00'
 AND COLUMNS.COLUMN_NAME = 'INV_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.INV_00
 ADD INV_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [INV_ID] de relação com o cabeçalho no novo [INV_ID_OLD] bkp 
 UPDATE INV_00
 SET    INV_ID_OLD = INV_ID
 WHERE  INV_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_INV_ID pra gerar os novos IDs do campo [INV_ID] 
 UPDATE INV_00
 SET    INV_ID = NEXT VALUE FOR SEQ_INV_ID
 GO 
  
 -- Recria índice Primary Key da table  [INV_ID] para o campo [INV_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'INV_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [INV_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'INV_00_PK')
             ALTER TABLE dbo.INV_00
             DROP CONSTRAINT INV_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.INV_00
 ADD CONSTRAINT INV_00_pk 
 PRIMARY KEY CLUSTERED (INV_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [INV_00] 
 REBUILD 
 END
 GO
  
