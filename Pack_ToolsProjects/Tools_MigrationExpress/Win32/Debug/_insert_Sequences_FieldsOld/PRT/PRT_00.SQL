 -- Cria o SEQUENCE para o campo [PRT_ID] da table [PRT_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_PRT_ID'))
  CREATE SEQUENCE dbo.SEQ_PRT_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [PRT_ID_OLD] pra receber IDs do campo [PRT_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'PRT_00'
 AND COLUMNS.COLUMN_NAME = 'PRT_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.PRT_00
 ADD PRT_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [PRT_ID] de relação com o cabeçalho no novo [PRT_ID_OLD] bkp 
 UPDATE PRT_00
 SET    PRT_ID_OLD = PRT_ID
 WHERE  PRT_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_PRT_ID pra gerar os novos IDs do campo [PRT_ID] 
 UPDATE PRT_00
 SET    PRT_ID = NEXT VALUE FOR SEQ_PRT_ID
 GO 
  
 -- Recria índice Primary Key da table  [PRT_ID] para o campo [PRT_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'PRT_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [PRT_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'PRT_00_PK')
             ALTER TABLE dbo.PRT_00
             DROP CONSTRAINT PRT_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.PRT_00
 ADD CONSTRAINT PRT_00_pk 
 PRIMARY KEY CLUSTERED (PRT_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [PRT_00] 
 REBUILD 
 END
 GO
  
