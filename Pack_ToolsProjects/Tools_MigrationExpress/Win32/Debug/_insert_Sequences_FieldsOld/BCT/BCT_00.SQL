 -- Cria o SEQUENCE para o campo [BCT_ID] da table [BCT_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_BCT_ID'))
  CREATE SEQUENCE dbo.SEQ_BCT_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [BCT_ID_OLD] pra receber IDs do campo [BCT_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'BCT_00'
 AND COLUMNS.COLUMN_NAME = 'BCT_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.BCT_00
 ADD BCT_ID_OLD int NULL 
 END; 
 GO 
  
 -- Guarda [BCT_ID] de relação com o cabeçalho no novo [BCT_ID_OLD] bkp 
 UPDATE BCT_00
 SET    BCT_ID_OLD = BCT_ID
 WHERE  BCT_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_BCT_ID pra gerar os novos IDs do campo [BCT_ID] 
 UPDATE BCT_00
 SET    BCT_ID = NEXT VALUE FOR SEQ_BCT_ID
 GO 
  
 -- Recria índice Primary Key da table  [BCT_ID] para o campo [BCT_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'BCT_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [BCT_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'BCT_00_PK')
             ALTER TABLE dbo.BCT_00
             DROP CONSTRAINT BCT_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.BCT_00
 ADD CONSTRAINT BCT_00_pk 
 PRIMARY KEY CLUSTERED (BCT_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [BCT_00] 
 REBUILD 
 END
 GO
