 -- Cria o SEQUENCE para o campo [PPC_ID] da table [PPC_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_PPC_ID'))
  CREATE SEQUENCE dbo.SEQ_PPC_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [PPC_ID_OLD] pra receber IDs do campo [PPC_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'PPC_00'
 AND COLUMNS.COLUMN_NAME = 'PPC_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.PPC_00
 ADD PPC_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [PPC_ID] de relação com o cabeçalho no novo [PPC_ID_OLD] bkp 
 UPDATE PPC_00
 SET    PPC_ID_OLD = PPC_ID
 WHERE  PPC_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_PPC_ID pra gerar os novos IDs do campo [PPC_ID] 
 UPDATE PPC_00
 SET    PPC_ID = NEXT VALUE FOR SEQ_PPC_ID
 GO 
  
 -- Recria índice Primary Key da table  [PPC_ID] para o campo [PPC_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'PPC_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [PPC_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'PPC_00_PK')
             ALTER TABLE dbo.PPC_00
             DROP CONSTRAINT PPC_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.PPC_00
 ADD CONSTRAINT PPC_00_pk 
 PRIMARY KEY CLUSTERED (PPC_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [PPC_00] 
 REBUILD 
 END
 GO
  
