 -- Cria o SEQUENCE para o campo [CTR_02_ID] da table [CTR_00_02]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CTR_02_ID'))
  CREATE SEQUENCE dbo.SEQ_CTR_02_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [CTR_02_ID_OLD] pra receber IDs do campo [CTR_02_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CTR_00_02'
 AND COLUMNS.COLUMN_NAME = 'CTR_02_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CTR_00_02
 ADD CTR_02_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [CTR_ID_OLD] pra receber IDs do campo [CTR_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CTR_00_02'
 AND COLUMNS.COLUMN_NAME = 'CTR_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CTR_00_02
 ADD CTR_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [CTR_02_ID] de relação com o cabeçalho no novo [CTR_02_ID_OLD] bkp 
 UPDATE CTR_00_02
 SET    CTR_02_ID_OLD = CTR_02_ID
 WHERE  CTR_02_ID_OLD IS NULL 
 GO 
  
 -- Guarda [CTR_ID] de relação com o cabeçalho no novo [CTR_ID_OLD] bkp 
 UPDATE CTR_00_02
 SET    CTR_ID_OLD = CTR_ID
 WHERE  CTR_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_CTR_02_ID pra gerar os novos IDs do campo [CTR_02_ID] 
 UPDATE CTR_00_02
 SET    CTR_02_ID = NEXT VALUE FOR SEQ_CTR_02_ID
 WHERE  CTR_02_ID_OLD IS NULL 
 GO 
  
 -- Recria índice Primary Key da table  [CTR_02_ID] para o campo [CTR_02_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CTR_00_02'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CTR_00_02]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CTR_00_02_PK')
             ALTER TABLE dbo.CTR_00_02
             DROP CONSTRAINT CTR_00_02_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CTR_00_02
 ADD CONSTRAINT CTR_00_02_pk 
 PRIMARY KEY CLUSTERED (CTR_02_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CTR_00_02] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [CTR_00] campo [CTR_ID], na table filha [CTR_00_02] campo [CTR_02_ID]
 UPDATE CTR_00_02 -- FILHA
 SET CTR_00_02.CTR_ID = T2.CTR_ID -- FILHA  RECEBE DA MÃE
 FROM CTR_00_02 -- FILHA
 INNER JOIN CTR_00 T2 ON (CTR_00_02.CTR_ID_OLD = T2.CTR_ID_OLD)
 AND (CTR_00_02.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
