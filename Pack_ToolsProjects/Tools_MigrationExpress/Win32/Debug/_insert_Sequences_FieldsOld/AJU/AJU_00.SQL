 -- Cria o SEQUENCE para o campo [AJU_ID] da table [AJU_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_AJU_ID'))
  CREATE SEQUENCE dbo.SEQ_AJU_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [AJU_ID_OLD] pra receber IDs do campo [AJU_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'AJU_00'
 AND COLUMNS.COLUMN_NAME = 'AJU_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.AJU_00
 ADD AJU_ID_OLD int NULL 
 END; 
 GO
  
 -- "MOTHER" Guarda [AJU_ID] de relação com o cabeçalho no novo [AJU_ID_OLD] bkp 
 UPDATE AJU_00
 SET    AJU_ID_OLD = AJU_ID
 WHERE  AJU_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_AJU_ID pra gerar os novos IDs do campo [AJU_ID] 
 UPDATE AJU_00
 SET    AJU_ID = NEXT VALUE FOR SEQ_AJU_ID
 GO 
  
 -- Recria índice Primary Key da table  [AJU_ID] para o campo [AJU_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'AJU_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [AJU_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'AJU_00_PK')
             ALTER TABLE dbo.AJU_00
             DROP CONSTRAINT AJU_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.AJU_00
