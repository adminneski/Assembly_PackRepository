 -- Cria o SEQUENCE para o campo [CLT_ID] da table [CLT_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CLT_ID'))
  CREATE SEQUENCE dbo.SEQ_CLT_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [CLT_ID_OLD] pra receber IDs do campo [CLT_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CLT_00'
 AND COLUMNS.COLUMN_NAME = 'CLT_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CLT_00
 ADD CLT_ID_OLD int NULL 
 END; 
 GO 
 
  
 -- Guarda [CLT_ID] de relação com o cabeçalho no novo [CLT_ID_OLD] bkp 
 UPDATE CLT_00
 SET    CLT_ID_OLD = CLT_ID
 WHERE  CLT_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_CLT_ID pra gerar os novos IDs do campo [CLT_ID] 
 UPDATE CLT_00
 SET    CLT_ID = NEXT VALUE FOR SEQ_CLT_ID
 GO 
  
 -- Recria índice Primary Key da table  [CLT_ID] para o campo [CLT_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CLT_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CLT_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CLT_00_PK')
             ALTER TABLE dbo.CLT_00
             DROP CONSTRAINT CLT_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CLT_00
 ADD CONSTRAINT CLT_00_pk 
 PRIMARY KEY CLUSTERED (CLT_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CLT_00] 
 REBUILD 
 END
 GO
  
