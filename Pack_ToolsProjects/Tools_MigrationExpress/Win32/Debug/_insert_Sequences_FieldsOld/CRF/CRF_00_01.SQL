 -- Cria o SEQUENCE para o campo [CRF_01_ID] da table [CRF_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CRF_01_ID'))
  CREATE SEQUENCE dbo.SEQ_CRF_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [CRF_01_ID_OLD] pra receber IDs do campo [CRF_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CRF_00_01'
 AND COLUMNS.COLUMN_NAME = 'CRF_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CRF_00_01
 ADD CRF_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [CRF_ID_OLD] pra receber IDs do campo [CRF_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CRF_00_01'
 AND COLUMNS.COLUMN_NAME = 'CRF_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CRF_00_01
 ADD CRF_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [CRF_01_ID] de relação com o cabeçalho no novo [CRF_01_ID_OLD] bkp 
 UPDATE CRF_00_01
 SET    CRF_01_ID_OLD = CRF_01_ID
 WHERE  CRF_01_ID_OLD IS NULL 
 GO 
  
 -- Guarda [CRF_ID] de relação com o cabeçalho no novo [CRF_ID_OLD] bkp 
 UPDATE CRF_00_01
 SET    CRF_ID_OLD = CRF_ID
 WHERE  CRF_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_CRF_01_ID pra gerar os novos IDs do campo [CRF_01_ID] 
 UPDATE CRF_00_01
 SET    CRF_01_ID = NEXT VALUE FOR SEQ_CRF_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [CRF_01_ID] para o campo [CRF_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CRF_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CRF_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CRF_00_01_PK')
             ALTER TABLE dbo.CRF_00_01
             DROP CONSTRAINT CRF_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CRF_00_01
 ADD CONSTRAINT CRF_00_01_pk 
 PRIMARY KEY CLUSTERED (CRF_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CRF_00_01] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [CRF_00] campo [CRF_ID], na table filha [CRF_00_01] campo [CRF_01_ID]
 UPDATE CRF_00_01 -- FILHA
 SET CRF_00_01.CRF_ID = T2.CRF_ID -- FILHA  RECEBE DA MÃE
 FROM CRF_00_01 -- FILHA
 INNER JOIN CRF_00 T2 ON (CRF_00_01.CRF_ID_OLD = T2.CRF_ID_OLD)
 AND (CRF_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
