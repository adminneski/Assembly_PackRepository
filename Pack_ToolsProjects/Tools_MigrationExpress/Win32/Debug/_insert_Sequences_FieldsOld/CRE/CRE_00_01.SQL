 -- Criando campo table MÂE [CRE_ID_OLD] pra receber IDs do campo [CRE_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CRE_00_01'
 AND COLUMNS.COLUMN_NAME = 'CRE_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CRE_00_01
 ADD CRE_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [CRE_ID] de relação com o cabeçalho no novo [CRE_ID_OLD] bkp 
 UPDATE CRE_00_01
 SET    CRE_ID_OLD = CRE_ID
 WHERE  CRE_ID_OLD IS NULL 
 GO
  
-- Atualiza novos ids da mãe [CRE_00] campo [CRE_ID], na table filha [CRE_00_01] campo [CRE_ID]
 UPDATE CRE_00_01 -- FILHA
 SET CRE_00_01.CRE_ID = T2.CRE_ID -- FILHA  RECEBE DA MÃE
 FROM CRE_00_01 -- FILHA
 INNER JOIN CRE_00 T2 ON (CRE_00_01.CRE_ID_OLD = T2.CRE_ID_OLD)
 AND (CRE_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO
 
 -- Recria índice Primary Key da table  [CRE_ID] para o campo [CRE_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CRE_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CRE_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CRE_00_01_PK')
             ALTER TABLE dbo.CRE_00_01
             DROP CONSTRAINT CRE_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CRE_00_01
 ADD CONSTRAINT CRE_00_01_pk 
 PRIMARY KEY CLUSTERED (CRE_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CRE_00_01] 
 REBUILD 
 END
 GO