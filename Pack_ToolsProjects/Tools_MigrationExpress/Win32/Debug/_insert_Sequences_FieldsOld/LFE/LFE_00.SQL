 -- Cria o SEQUENCE para o campo [LFE_ID] da table [LFE_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_LFE_ID'))
  CREATE SEQUENCE dbo.SEQ_LFE_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
 -- Criando campo [LFE_ID_OLD] pra receber IDs do campo [LFE_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'LFE_00'
 AND COLUMNS.COLUMN_NAME = 'LFE_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.LFE_00
 ADD LFE_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [LFE_ID] de relação com o cabeçalho no novo [LFE_ID_OLD] bkp 
 UPDATE LFE_00
 SET    LFE_ID_OLD = LFE_ID
 WHERE  LFE_ID_OLD IS NULL 
 GO 
  
 -- Criando campo [LFE_INTEGRACAO_OLD] pra receber BKP DAS INTEGRAÇÕES do campo [LFE_INTEGRACAO] 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'LFE_00'
             AND   COLUMNS.COLUMN_NAME = 'LFE_INTEGRACAO')
 BEGIN 
    IF NOT EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
                    WHERE COLUMNS.TABLE_NAME = 'LFE_00'
                    AND COLUMNS.COLUMN_NAME  = 'LFE_INTEGRACAO_OLD')
    BEGIN 
       ALTER TABLE dbo.LFE_00 ADD LFE_INTEGRACAO_OLD VARCHAR(50) NULL 
    END; 
 END; 
 GO 
  
 -- Guarda [LFE_INTEGRACAO] de integrações no novo [LFE_INTEGRACAO_OLD] bkp 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'LFE_00'
             AND   COLUMNS.COLUMN_NAME = 'LFE_INTEGRACAO')
 BEGIN 
    UPDATE LFE_00
    SET    [LFE_INTEGRACAO_OLD] = LFE_INTEGRACAO
    WHERE  [LFE_INTEGRACAO_OLD] IS NULL 
 END; 
 GO 
  
 -- Executa o SEQ_LFE_ID pra gerar os novos IDs do campo [LFE_ID] 
 UPDATE LFE_00
 SET    LFE_ID = NEXT VALUE FOR SEQ_LFE_ID
 GO 
  
 -- Recria índice Primary Key da table  [LFE_ID] para o campo [LFE_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'LFE_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [LFE_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'LFE_00_PK')
             ALTER TABLE dbo.LFE_00
             DROP CONSTRAINT LFE_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.LFE_00
 ADD CONSTRAINT LFE_00_pk 
 PRIMARY KEY CLUSTERED (LFE_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [LFE_00] 
 REBUILD 
 END
 GO