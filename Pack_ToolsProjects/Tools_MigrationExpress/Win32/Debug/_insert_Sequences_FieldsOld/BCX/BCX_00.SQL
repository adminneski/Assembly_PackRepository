 -- Cria o SEQUENCE para o campo [BCX_ID] da table [BCX_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_BCX_ID'))
  CREATE SEQUENCE dbo.SEQ_BCX_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "INTEGRACÃO" Criando campo [BCX_INTEGRACAO_OLD] pra receber BKP DAS INTEGRAÇÕES do campo [BCX_INTEGRACAO] 
 IF (SELECT COUNT(COLUMNS.COLUMN_NAME) FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'BCX_00'
             AND   COLUMNS.COLUMN_NAME = 'BCX_INTEGRACAO') > 0
 BEGIN 
    IF NOT EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
                    WHERE COLUMNS.TABLE_NAME = 'BCX_00'
                    AND COLUMNS.COLUMN_NAME  = 'BCX_INTEGRACAO_OLD')
    BEGIN 
       ALTER TABLE dbo.BCX_00 ADD BCX_INTEGRACAO_OLD VARCHAR(50) NULL 
    END; 
 END; 
 GO 
  
 -- "INTEGRACÃO" Guarda [BCX_INTEGRACAO] de integrações no novo [BCX_INTEGRACAO_OLD] bkp 
 IF ( SELECT COUNT(COLUMNS.COLUMN_NAME) FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'BCX_00'
             AND   COLUMNS.COLUMN_NAME = 'BCX_INTEGRACAO') > 0
 BEGIN 
    UPDATE BCX_00
    SET    [BCX_INTEGRACAO_OLD] = BCX_INTEGRACAO
    WHERE  [BCX_INTEGRACAO_OLD] IS NULL 
 END; 
 GO 
  
 -- "1408" Criando campo [BCX_ID_OLD] pra receber IDs do campo [BCX_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'BCX_00'
 AND COLUMNS.COLUMN_NAME = 'BCX_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.BCX_00
 ADD BCX_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [BCX_ID] de relação com o cabeçalho no novo [BCX_ID_OLD] bkp 
 UPDATE BCX_00
 SET    BCX_ID_OLD = BCX_ID
 WHERE  BCX_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_BCX_ID pra gerar os novos IDs do campo [BCX_ID] 
 UPDATE BCX_00
 SET    BCX_ID = NEXT VALUE FOR SEQ_BCX_ID
 GO 
  
 -- Recria índice Primary Key da table  [BCX_ID] para o campo [BCX_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'BCX_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [BCX_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'BCX_00_PK')
             ALTER TABLE dbo.BCX_00
             DROP CONSTRAINT BCX_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.BCX_00
 ADD CONSTRAINT BCX_00_pk 
 PRIMARY KEY CLUSTERED (BCX_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [BCX_00] 
 REBUILD 
 END
GO