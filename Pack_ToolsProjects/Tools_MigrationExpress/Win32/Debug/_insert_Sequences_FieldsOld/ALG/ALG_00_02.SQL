 -- Cria o SEQUENCE para o campo [ALG_02_ID] da table [ALG_00_02]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_ALG_02_ID'))
  CREATE SEQUENCE dbo.SEQ_ALG_02_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [ALG_ID_OLD] pra receber IDs do campo [ALG_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'ALG_00_02'
 AND COLUMNS.COLUMN_NAME = 'ALG_02_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.ALG_00_02
 ADD ALG_02_ID_OLD int NULL 
 END 
 GO   
  
 -- Criando campo [ALG_ID_OLD] pra receber IDs do campo [ALG_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'ALG_00_02'
 AND COLUMNS.COLUMN_NAME = 'ALG_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.ALG_00_02
 ADD ALG_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [ALG_ID] de relação com o cabeçalho no novo [ALG_ID_OLD] bkp 
 UPDATE ALG_00_02
 SET    ALG_02_ID_OLD = ALG_02_ID,
 ALG_ID_OLD = ALG_ID
 WHERE  ALG_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_ALG_02_ID pra gerar os novos IDs do campo [ALG_02_ID] 
 UPDATE ALG_00_02
 SET    ALG_02_ID = NEXT VALUE FOR SEQ_ALG_02_ID
 GO 
  
 -- Recria índice Primary Key da table  [ALG_02_ID] para o campo [ALG_02_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'ALG_00_02'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [ALG_00_02]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'ALG_00_02_PK')
             ALTER TABLE dbo.ALG_00_02
             DROP CONSTRAINT ALG_00_02_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.ALG_00_02
 ADD CONSTRAINT ALG_00_02_pk 
 PRIMARY KEY CLUSTERED (ALG_02_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [ALG_00_02] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [ALG_00] campo [ALG_ID], na table filha [ALG_00_02] campo [ALG_02_ID]
 UPDATE ALG_00_02 -- FILHA
 SET ALG_00_02.ALG_ID = T2.ALG_ID -- FILHA  RECEBE DA MÃE
 FROM ALG_00_02 -- FILHA
 INNER JOIN ALG_00 T2 ON (ALG_00_02.ALG_ID_OLD = T2.ALG_ID_OLD)
 AND (ALG_00_02.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO