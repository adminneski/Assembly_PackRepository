 -- Cria o SEQUENCE para o campo [MTL_ID] da table [MTL_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_MTL_ID'))
  CREATE SEQUENCE dbo.SEQ_MTL_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [MTL_ID_OLD] pra receber IDs do campo [MTL_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'MTL_00'
 AND COLUMNS.COLUMN_NAME = 'MTL_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.MTL_00
 ADD MTL_ID_OLD int NULL 
 END; 
 GO 
  
 -- "1267" Guarda [MTL_ID] de relação com o cabeçalho no novo [MTL_ID_OLD] bkp 
 UPDATE MTL_00
 SET    MTL_ID_OLD = MTL_ID
 WHERE  MTL_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_MTL_ID pra gerar os novos IDs do campo [MTL_ID] 
 UPDATE MTL_00
 SET    MTL_ID = NEXT VALUE FOR SEQ_MTL_ID
 GO 
  
 -- Recria índice Primary Key da table  [MTL_ID] para o campo [MTL_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'MTL_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [MTL_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'MTL_00_idx')
             ALTER TABLE dbo.MTL_00
             DROP CONSTRAINT MTL_00_idx 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.MTL_00
 ADD CONSTRAINT MTL_00_pk 
 PRIMARY KEY CLUSTERED (MTL_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [MTL_00] 
 REBUILD 
 END
 GO
  
