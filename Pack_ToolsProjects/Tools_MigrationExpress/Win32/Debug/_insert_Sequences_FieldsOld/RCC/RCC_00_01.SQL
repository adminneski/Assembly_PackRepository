 -- Cria o SEQUENCE para o campo [RCC_01_ID] da table [RCC_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_RCC_01_ID'))
  CREATE SEQUENCE dbo.SEQ_RCC_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
  
 -- "1035" Criando campo [RCC_01_ID_OLD] pra receber IDs do campo [RCC_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'RCC_00_01'
 AND COLUMNS.COLUMN_NAME = 'RCC_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.RCC_00_01
 ADD RCC_01_ID_OLD int NULL 
 END; 
 GO 
  
 -- "1146" Criando campo [RCC_ID_OLD] pra receber IDs do campo [RCC_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'RCC_00_01'
 AND COLUMNS.COLUMN_NAME = 'RCC_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.RCC_00_01
 ADD RCC_ID_OLD int NULL 
 END; 
 GO 
  
 -- "1267" Guarda [RCC_01_ID] de relação com o cabeçalho no novo [RCC_01_ID_OLD] bkp 
 UPDATE RCC_00_01
 SET    RCC_01_ID_OLD = RCC_01_ID
 WHERE  RCC_01_ID_OLD IS NULL 
 GO 
  
 -- "MOTHER" Guarda [RCC_ID] de relação com o cabeçalho no novo [RCC_ID_OLD] bkp 
 UPDATE RCC_00_01
 SET    RCC_ID_OLD = RCC_ID
 WHERE  RCC_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_RCC_01_ID pra gerar os novos IDs do campo [RCC_01_ID] 
 UPDATE RCC_00_01
 SET    RCC_01_ID = NEXT VALUE FOR SEQ_RCC_01_ID
 GO 
   
-- Atualiza novos ids da mãe [RCC_00] campo [RCC_ID], na table filha [RCC_00_01] campo [RCC_01_ID]
 UPDATE RCC_00_01 -- FILHA
 SET RCC_00_01.RCC_ID = T2.RCC_ID -- FILHA  RECEBE DA MÃE
 FROM RCC_00_01 -- FILHA
 INNER JOIN RCC_00 T2 ON (RCC_00_01.RCC_ID_OLD = T2.RCC_ID_OLD) --RELACIONA FILHA COM MAE
 GO 
  
