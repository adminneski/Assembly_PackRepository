 -- Cria o SEQUENCE para o campo [RCC_ID] da table [RCC_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_RCC_ID'))
  CREATE SEQUENCE dbo.SEQ_RCC_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [RCC_ID_OLD] pra receber IDs do campo [RCC_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'RCC_00'
 AND COLUMNS.COLUMN_NAME = 'RCC_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.RCC_00
 ADD RCC_ID_OLD int NULL 
 END; 
 GO 
 
 -- "MOTHER" Guarda [RCC_ID] de relação com o cabeçalho no novo [RCC_ID_OLD] bkp 
 UPDATE RCC_00
 SET    RCC_ID_OLD = RCC_ID
 WHERE  RCC_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_RCC_ID pra gerar os novos IDs do campo [RCC_ID] 
 UPDATE RCC_00
 SET    RCC_ID = NEXT VALUE FOR SEQ_RCC_ID
 GO 