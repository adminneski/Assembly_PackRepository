 -- Cria o SEQUENCE para o campo [ORS_ID] da table [ORS_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_ORS_ID'))
  CREATE SEQUENCE dbo.SEQ_ORS_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- "1058" Criando campo [ORS_ID_OLD] pra receber IDs do campo [ORS_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'ORS_00'
 AND COLUMNS.COLUMN_NAME = 'ORS_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.ORS_00
 ADD ORS_ID_OLD int NULL 
 END; 
 GO
 
 -- "INTEGRACÃO" Criando campo [ORS_INTEGRACAO_OLD] pra receber BKP DAS INTEGRAÇÕES do campo [ORS_INTEGRACAO] 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'ORS_00'
             AND   COLUMNS.COLUMN_NAME = 'ORS_INTEGRACAO')
 BEGIN 
    IF NOT EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
                    WHERE COLUMNS.TABLE_NAME = 'ORS_00'
                    AND COLUMNS.COLUMN_NAME  = 'ORS_INTEGRACAO_OLD')
    BEGIN 
       ALTER TABLE dbo.ORS_00 ADD ORS_INTEGRACAO_OLD VARCHAR(50) NULL 
    END; 
 END; 
 GO 
  
 -- "INTEGRACÃO" Guarda [ORS_INTEGRACAO] de integrações no novo [ORS_INTEGRACAO_OLD] bkp 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'ORS_00'
             AND   COLUMNS.COLUMN_NAME = 'ORS_INTEGRACAO')
 BEGIN 
    UPDATE ORS_00
    SET    [ORS_INTEGRACAO_OLD] = ORS_INTEGRACAO
    WHERE  [ORS_INTEGRACAO_OLD] IS NULL 
 END; 
 GO 
  
 -- "1267" Guarda [ORS_ID] de relação com o cabeçalho no novo [ORS_ID_OLD] bkp 
 UPDATE ORS_00
 SET    ORS_ID_OLD = ORS_ID
 WHERE  ORS_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_ORS_ID pra gerar os novos IDs do campo [ORS_ID] 
 UPDATE ORS_00
 SET    ORS_ID = NEXT VALUE FOR SEQ_ORS_ID
 GO 
  
