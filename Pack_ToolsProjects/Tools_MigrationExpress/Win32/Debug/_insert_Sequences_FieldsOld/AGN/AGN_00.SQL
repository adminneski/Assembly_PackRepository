 -- Cria o SEQUENCE para o campo [AGN_ID] da table [AGN_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_AGN_ID'))
  CREATE SEQUENCE dbo.SEQ_AGN_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
 -- Criando campo [AGN_ID_OLD] pra receber IDs do campo [AGN_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'AGN_00'
 AND COLUMNS.COLUMN_NAME = 'AGN_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.AGN_00
 ADD AGN_ID_OLD int NULL 
 END; 
 GO 
  
 -- Guarda [AGN_ID] de relação com o cabeçalho no novo [AGN_ID_OLD] bkp 
 UPDATE AGN_00
 SET    AGN_ID_OLD = AGN_ID
 WHERE  AGN_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_AGN_ID pra gerar os novos IDs do campo [AGN_ID] 
 UPDATE AGN_00
 SET    AGN_ID = NEXT VALUE FOR SEQ_AGN_ID
 GO 
  
 -- Recria índice Primary Key da table  [AGN_ID] para o campo [AGN_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'AGN_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [AGN_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'AGN_00_PK')
             ALTER TABLE dbo.AGN_00
             DROP CONSTRAINT AGN_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.AGN_00
 ADD CONSTRAINT AGN_00_pk 
 PRIMARY KEY CLUSTERED (AGN_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [AGN_00] 
 REBUILD 
 END
 GO