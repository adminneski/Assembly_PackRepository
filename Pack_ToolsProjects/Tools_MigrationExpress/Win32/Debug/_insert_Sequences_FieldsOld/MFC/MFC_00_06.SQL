 -- Cria o SEQUENCE para o campo [MFC_06_ID] da table [MFC_00_06]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_MFC_06_ID'))
  CREATE SEQUENCE dbo.SEQ_MFC_06_ID
  AS int
  START WITH 1
  NO MINVALUE
  NO MAXVALUE
  NO CYCLE
  CACHE
  GO

-- "1070" Criando campo [MFC_06_ID_OLD] pra receber IDs do campo [MFC_06_ID] PARA GUARDAR OS IDs ANTIGOS
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS
 WHERE COLUMNS.TABLE_NAME = 'MFC_00_06'
 AND COLUMNS.COLUMN_NAME = 'MFC_06_ID_OLD')
 BEGIN
 ALTER TABLE dbo.MFC_00_06
 ADD MFC_06_ID_OLD int NULL
 END;
 GO

 -- "1070" Criando campo [MFC_ID_OLD] pra receber IDs do campo [MFC_ID] PARA GUARDAR OS IDs ANTIGOS
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS
 WHERE COLUMNS.TABLE_NAME = 'MFC_00_06'
 AND COLUMNS.COLUMN_NAME = 'MFC_ID_OLD')
 BEGIN
 ALTER TABLE dbo.MFC_00_06
 ADD MFC_ID_OLD int NULL
 END;
 GO

 -- "MOTHER" Guarda [MFC_ID] de relação com o cabeçalho no novo [MFC_ID_OLD] bkp
 UPDATE MFC_00_06
 SET    MFC_ID_OLD = MFC_ID,
 MFC_06_ID_OLD = MFC_06_ID
 WHERE  MFC_ID_OLD IS NULL
 GO

 -- Executa o SEQ_MFC_06_ID pra gerar os novos IDs do campo [MFC_06_ID]
 UPDATE MFC_00_06
 SET    MFC_06_ID = NEXT VALUE FOR SEQ_MFC_06_ID
 GO

 -- Recria índice Primary Key da table  [MFC_06_ID] para o campo [MFC_06_ID]
 IF EXISTS ( SELECT count(table_name)
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE
    WHERE table_name = 'MFC_00_06'
    HAVING COUNT(*) > 1 )
 BEGIN
 -- DESABILITA INDICE
 ALTER INDEX ALL ON [MFC_00_06]
 DISABLE

 -- VERIFICA SE EXISTE O INDICE E O APAGA
 IF  EXISTS (SELECT *
             FROM sys.indexes AS si
             WHERE si.name = 'MFC_00_06_PK')
             ALTER TABLE dbo.MFC_00_06
             DROP CONSTRAINT MFC_00_06_pk

 -- ALTERA A TABLE CRIANDO O INDICE
 ALTER TABLE dbo.MFC_00_06
 ADD CONSTRAINT MFC_00_06_pk
 PRIMARY KEY CLUSTERED (MFC_06_ID)
 WITH (
   PAD_INDEX = OFF,
   FILLFACTOR = 90,
   IGNORE_DUP_KEY = OFF,
   STATISTICS_NORECOMPUTE = OFF,
   ALLOW_ROW_LOCKS = ON,
   ALLOW_PAGE_LOCKS = ON)
 ON [PRIMARY]

 -- REABILITA INDICES
 ALTER INDEX ALL ON [MFC_00_06]
 REBUILD
 END
 GO

