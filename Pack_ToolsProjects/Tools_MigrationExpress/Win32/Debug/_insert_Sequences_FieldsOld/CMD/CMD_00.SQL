 -- Cria o SEQUENCE para o campo [CMD_ID] da table [CMD_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CMD_ID'))
  CREATE SEQUENCE dbo.SEQ_CMD_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [CMD_ID_OLD] pra receber IDs do campo [CMD_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CMD_00'
 AND COLUMNS.COLUMN_NAME = 'CMD_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CMD_00
 ADD CMD_ID_OLD int NULL 
 END; 
 GO 
  
 -- Criando campo [CMD_ID_OLD] pra receber IDs do campo [CMD_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CMD_00'
 AND COLUMNS.COLUMN_NAME = 'CMD_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CMD_00
 ADD CMD_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [CMD_ID] de relação com o cabeçalho no novo [CMD_ID_OLD] bkp 
 UPDATE CMD_00
 SET    CMD_ID_OLD = CMD_ID
 WHERE  CMD_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_CMD_ID pra gerar os novos IDs do campo [CMD_ID] 
 UPDATE CMD_00
 SET    CMD_ID = NEXT VALUE FOR SEQ_CMD_ID
 GO 
  
 -- Recria índice Primary Key da table  [CMD_ID] para o campo [CMD_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CMD_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CMD_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CMD_00_PK')
             ALTER TABLE dbo.CMD_00
             DROP CONSTRAINT CMD_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CMD_00
 ADD CONSTRAINT CMD_00_pk 
 PRIMARY KEY CLUSTERED (CMD_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CMD_00] 
 REBUILD 
 END
 GO