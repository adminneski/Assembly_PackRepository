 -- Cria o SEQUENCE para o campo [PVD_ID] da table [PVD_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_PVD_ID'))
  CREATE SEQUENCE dbo.SEQ_PVD_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [PVD_ID_OLD] pra receber IDs do campo [PVD_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'PVD_00'
 AND COLUMNS.COLUMN_NAME = 'PVD_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.PVD_00
 ADD PVD_ID_OLD int NULL 
 END; 
 GO 
 
 -- "MOTHER" Guarda [PVD_ID] de relação com o cabeçalho no novo [PVD_ID_OLD] bkp 
 UPDATE PVD_00
 SET    PVD_ID_OLD = PVD_ID
 WHERE  PVD_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_PVD_ID pra gerar os novos IDs do campo [PVD_ID] 
 UPDATE PVD_00
 SET    PVD_ID = NEXT VALUE FOR SEQ_PVD_ID
 GO 
  
 -- Recria índice Primary Key da table  [PVD_ID] para o campo [PVD_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'PVD_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [PVD_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'PVD_00_PK')
             ALTER TABLE dbo.PVD_00
             DROP CONSTRAINT PVD_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.PVD_00
 ADD CONSTRAINT PVD_00_pk 
 PRIMARY KEY CLUSTERED (PVD_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [PVD_00] 
 REBUILD 
 END
 GO
  
