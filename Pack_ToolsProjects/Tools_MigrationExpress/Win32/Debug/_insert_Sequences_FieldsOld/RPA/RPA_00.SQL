 -- Cria o SEQUENCE para o campo [RPA_ID] da table [RPA_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_RPA_ID'))
  CREATE SEQUENCE dbo.SEQ_RPA_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [RPA_ID_OLD] pra receber IDs do campo [RPA_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'RPA_00'
 AND COLUMNS.COLUMN_NAME = 'RPA_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.RPA_00
 ADD RPA_ID_OLD int NULL 
 END; 
 GO 
 
  
 -- "MOTHER" Guarda [RPA_ID] de relação com o cabeçalho no novo [RPA_ID_OLD] bkp 
 UPDATE RPA_00
 SET    RPA_ID_OLD = RPA_ID
 WHERE  RPA_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_RPA_ID pra gerar os novos IDs do campo [RPA_ID] 
 UPDATE RPA_00
 SET    RPA_ID = NEXT VALUE FOR SEQ_RPA_ID
 GO 
  
 -- Recria índice Primary Key da table  [RPA_ID] para o campo [RPA_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'RPA_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [RPA_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'RPA_00_PK')
             ALTER TABLE dbo.RPA_00
             DROP CONSTRAINT RPA_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.RPA_00
 ADD CONSTRAINT RPA_00_pk 
 PRIMARY KEY CLUSTERED (RPA_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [RPA_00] 
 REBUILD 
 END
 GO
  
