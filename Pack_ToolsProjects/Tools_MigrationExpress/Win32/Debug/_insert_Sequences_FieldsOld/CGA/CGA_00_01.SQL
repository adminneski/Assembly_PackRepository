 -- Cria o SEQUENCE para o campo [CGA_01_ID] da table [CGA_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CGA_01_ID'))
 CREATE SEQUENCE dbo.SEQ_CGA_01_ID
 AS int 
 START WITH 1
 NO MINVALUE 
 NO MAXVALUE 
 NO CYCLE 
 CACHE 
 GO 
  
 -- Criando campo [CGA_01_ID_OLD] pra receber IDs do campo [CGA_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CGA_00_01'
 AND COLUMNS.COLUMN_NAME = 'CGA_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CGA_00_01
 ADD CGA_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [CGA_ID_OLD] pra receber IDs do campo [CGA_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CGA_00_01'
 AND COLUMNS.COLUMN_NAME = 'CGA_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CGA_00_01
 ADD CGA_ID_OLD int NULL 
 END 
 GO 

 -- Criando campo [FAT_ID] pra receber IDs do campo [FAT_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CGA_00_01'
 AND COLUMNS.COLUMN_NAME = 'FAT_ID')
 BEGIN 
 ALTER TABLE dbo.CGA_00_01
 ADD FAT_ID int NULL 
 END 
 GO 
  
 -- Guarda [CGA_01_ID] de relação com o cabeçalho no novo [CGA_01_ID_OLD] bkp 
 UPDATE CGA_00_01
 SET    CGA_01_ID_OLD = CGA_01_ID
 WHERE  CGA_01_ID_OLD IS NULL 
 GO 
  
 -- Guarda [CGA_ID] de relação com o cabeçalho no novo [CGA_ID_OLD] bkp 
 UPDATE CGA_00_01
 SET    CGA_ID_OLD = CGA_ID
 WHERE  CGA_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_CGA_01_ID pra gerar os novos IDs do campo [CGA_01_ID] 
 UPDATE CGA_00_01
 SET    CGA_01_ID = NEXT VALUE FOR SEQ_CGA_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [CGA_01_ID] para o campo [CGA_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CGA_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CGA_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CGA_00_01_PK')
             ALTER TABLE dbo.CGA_00_01
             DROP CONSTRAINT CGA_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CGA_00_01
 ADD CONSTRAINT CGA_00_01_pk 
 PRIMARY KEY CLUSTERED (CGA_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CGA_00_01] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [CGA_00] campo [CGA_ID], na table filha [CGA_00_01] campo [CGA_01_ID]
 UPDATE CGA_00_01 -- FILHA
 SET CGA_00_01.CGA_ID = T2.CGA_ID -- FILHA  RECEBE DA MÃE
 FROM CGA_00_01 -- FILHA
 INNER JOIN CGA_00 T2 ON (CGA_00_01.CGA_ID_OLD = T2.CGA_ID_OLD)
 AND (CGA_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO