 -- "1070" Criando campo [NFP_ID_OLD] pra receber IDs do campo [NFP_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'NFP_00_03'
 AND COLUMNS.COLUMN_NAME = 'NFP_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.NFP_00_03
 ADD NFP_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [NFP_ID] de relação com o cabeçalho no novo [NFP_ID_OLD] bkp 
 UPDATE NFP_00_03
 SET    NFP_ID_OLD = NFP_ID
 WHERE  NFP_ID_OLD IS NULL 
 GO 
   
 -- Recria índice Primary Key da table  [] para o campo [] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'NFP_00_03'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [NFP_00_03]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'NFP_00_03_idx')
             ALTER TABLE dbo.NFP_00_03
             DROP CONSTRAINT NFP_00_03_idx 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.NFP_00_03
 ADD CONSTRAINT NFP_00_03_pk 
 PRIMARY KEY CLUSTERED (NFP_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [NFP_00_03] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [NFP_00] campo [NFP_ID], na table filha [NFP_00_03] campo []
 UPDATE NFP_00_03 -- FILHA
 SET NFP_00_03.NFP_ID = T2.NFP_ID -- FILHA  RECEBE DA MÃE
 FROM NFP_00_03 -- FILHA
 INNER JOIN NFP_00 T2 ON (NFP_00_03.NFP_ID_OLD = T2.NFP_ID_OLD)
 AND (NFP_00_03.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
