 -- Cria o SEQUENCE para o campo [LIB_ID] da table [LIB_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_LIB_ID'))
  CREATE SEQUENCE dbo.SEQ_LIB_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [LIB_ID_OLD] pra receber IDs do campo [LIB_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'LIB_00'
 AND COLUMNS.COLUMN_NAME = 'LIB_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.LIB_00
 ADD LIB_ID_OLD int NULL 
 END; 
 GO 
  
 -- Guarda [LIB_ID] de relação com o cabeçalho no novo [LIB_ID_OLD] bkp 
 UPDATE LIB_00
 SET    LIB_ID_OLD = LIB_ID
 WHERE  LIB_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_LIB_ID pra gerar os novos IDs do campo [LIB_ID] 
 UPDATE LIB_00
 SET    LIB_ID = NEXT VALUE FOR SEQ_LIB_ID
 GO 
  
 -- Recria índice Primary Key da table  [LIB_ID] para o campo [LIB_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'LIB_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [LIB_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'LIB_00_PK')
             ALTER TABLE dbo.LIB_00
             DROP CONSTRAINT LIB_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.LIB_00
 ADD CONSTRAINT LIB_00_pk 
 PRIMARY KEY CLUSTERED (LIB_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [LIB_00] 
 REBUILD 
 END
 GO
  
