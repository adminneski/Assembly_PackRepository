 -- Cria o SEQUENCE para o campo [TLN_ID] da table [TLN_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_TLN_ID'))
  CREATE SEQUENCE dbo.SEQ_TLN_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [TLN_ID_OLD] pra receber IDs do campo [TLN_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'TLN_00'
 AND COLUMNS.COLUMN_NAME = 'TLN_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.TLN_00
 ADD TLN_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [TLN_ID] de relação com o cabeçalho no novo [TLN_ID_OLD] bkp 
 UPDATE TLN_00
 SET    TLN_ID_OLD = TLN_ID
 WHERE  TLN_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_TLN_ID pra gerar os novos IDs do campo [TLN_ID] 
 UPDATE TLN_00
 SET    TLN_ID = NEXT VALUE FOR SEQ_TLN_ID
 GO 
  
 -- Recria índice Primary Key da table  [TLN_ID] para o campo [TLN_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'TLN_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [TLN_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'PK_TLN_00')
             ALTER TABLE dbo.TLN_00
             DROP CONSTRAINT PK_TLN_00
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.TLN_00
 ADD CONSTRAINT TLN_00_pk 
 PRIMARY KEY CLUSTERED (TLN_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [TLN_00] 
 REBUILD 
 END
 GO
  
