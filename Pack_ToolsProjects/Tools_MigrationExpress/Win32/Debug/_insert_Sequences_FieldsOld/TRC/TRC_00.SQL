 -- Cria o SEQUENCE para o campo [TRC_ID] da table [TRC_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_TRC_ID'))
  CREATE SEQUENCE dbo.SEQ_TRC_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [TRC_ID_OLD] pra receber IDs do campo [TRC_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'TRC_00'
 AND COLUMNS.COLUMN_NAME = 'TRC_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.TRC_00
 ADD TRC_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [TRC_ID] de relação com o cabeçalho no novo [TRC_ID_OLD] bkp 
 UPDATE TRC_00
 SET    TRC_ID_OLD = TRC_ID
 WHERE  TRC_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_TRC_ID pra gerar os novos IDs do campo [TRC_ID] 
 UPDATE TRC_00
 SET    TRC_ID = NEXT VALUE FOR SEQ_TRC_ID
 GO 
  
 -- Recria índice Primary Key da table  [TRC_ID] para o campo [TRC_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'TRC_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [TRC_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'TRC_00_PK')
             ALTER TABLE dbo.TRC_00
             DROP CONSTRAINT TRC_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.TRC_00
 ADD CONSTRAINT TRC_00_pk 
 PRIMARY KEY CLUSTERED (TRC_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [TRC_00] 
 REBUILD 
 END
 GO
  
