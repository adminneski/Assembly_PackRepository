 -- Cria o SEQUENCE para o campo [CPS_ID] da table [CPS_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CPS_ID'))
  CREATE SEQUENCE dbo.SEQ_CPS_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [CPS_ID_OLD] pra receber IDs do campo [CPS_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CPS_00'
 AND COLUMNS.COLUMN_NAME = 'CPS_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CPS_00
 ADD CPS_ID_OLD int NULL 
 END; 
 GO
  
 -- Guarda [CPS_ID] de relação com o cabeçalho no novo [CPS_ID_OLD] bkp 
 UPDATE CPS_00
 SET    CPS_ID_OLD = CPS_ID
 WHERE  CPS_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_CPS_ID pra gerar os novos IDs do campo [CPS_ID] 
 UPDATE CPS_00
 SET    CPS_ID = NEXT VALUE FOR SEQ_CPS_ID
 GO 
  
 -- Recria índice Primary Key da table  [CPS_ID] para o campo [CPS_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CPS_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CPS_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CPS_00_PK')
             ALTER TABLE dbo.CPS_00
             DROP CONSTRAINT CPS_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CPS_00
 ADD CONSTRAINT CPS_00_pk 
 PRIMARY KEY CLUSTERED (CPS_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CPS_00] 
 REBUILD 
 END
 GO