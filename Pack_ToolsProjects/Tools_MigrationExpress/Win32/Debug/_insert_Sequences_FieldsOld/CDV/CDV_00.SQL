 -- Cria o SEQUENCE para o campo [CDV_ID] da table [CDV_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CDV_ID'))
  CREATE SEQUENCE dbo.SEQ_CDV_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [CDV_ID_OLD] pra receber IDs do campo [CDV_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CDV_00'
 AND COLUMNS.COLUMN_NAME = 'CDV_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CDV_00
 ADD CDV_ID_OLD int NULL 
 END; 
 GO 
  
 -- Guarda [CDV_ID] de relação com o cabeçalho no novo [CDV_ID_OLD] bkp 
 UPDATE CDV_00
 SET    CDV_ID_OLD = CDV_ID
 WHERE  CDV_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_CDV_ID pra gerar os novos IDs do campo [CDV_ID] 
 UPDATE CDV_00
 SET    CDV_ID = NEXT VALUE FOR SEQ_CDV_ID
 GO 
  
 -- Recria índice Primary Key da table  [CDV_ID] para o campo [CDV_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CDV_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CDV_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CDV_00_PK')
             ALTER TABLE dbo.CDV_00
             DROP CONSTRAINT CDV_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CDV_00
 ADD CONSTRAINT CDV_00_pk 
 PRIMARY KEY CLUSTERED (CDV_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CDV_00] 
 REBUILD 
 END
 GO
  
