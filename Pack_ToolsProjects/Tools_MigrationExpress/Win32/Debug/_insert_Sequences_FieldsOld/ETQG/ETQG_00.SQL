 -- Cria o SEQUENCE para o campo [ETQG_ID] da table [ETQG_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_ETQG_ID'))
  CREATE SEQUENCE dbo.SEQ_ETQG_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [ETQG_ID_OLD] pra receber IDs do campo [ETQG_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'ETQG_00'
 AND COLUMNS.COLUMN_NAME = 'ETQG_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.ETQG_00
 ADD ETQG_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [ETQG_ID] de relação com o cabeçalho no novo [ETQG_ID_OLD] bkp 
 UPDATE ETQG_00
 SET    ETQG_ID_OLD = ETQG_ID
 WHERE  ETQG_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_ETQG_ID pra gerar os novos IDs do campo [ETQG_ID] 
 UPDATE ETQG_00
 SET    ETQG_ID = NEXT VALUE FOR SEQ_ETQG_ID
 GO 
  
 -- Recria índice Primary Key da table  [ETQG_ID] para o campo [ETQG_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'ETQG_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [ETQG_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'ETQG_00_PK')
             ALTER TABLE dbo.ETQG_00
             DROP CONSTRAINT ETQG_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.ETQG_00
 ADD CONSTRAINT ETQG_00_pk 
 PRIMARY KEY CLUSTERED (ETQG_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [ETQG_00] 
 REBUILD 
 END
 GO