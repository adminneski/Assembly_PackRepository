 -- Cria o SEQUENCE para o campo [LFS_ID] da table [LFS_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_LFS_ID'))
  CREATE SEQUENCE dbo.SEQ_LFS_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [LFS_ID_OLD] pra receber IDs do campo [LFS_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'LFS_00'
 AND COLUMNS.COLUMN_NAME = 'LFS_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.LFS_00
 ADD LFS_ID_OLD int NULL 
 END; 
 GO 
  
 -- Guarda [LFS_ID] de relação com o cabeçalho no novo [LFS_ID_OLD] bkp 
 UPDATE LFS_00
 SET    LFS_ID_OLD = LFS_ID
 WHERE  LFS_ID_OLD IS NULL 
 GO 
  
 -- Criando campo [LFS_INTEGRACAO_OLD] pra receber BKP DAS INTEGRAÇÕES do campo [LFS_INTEGRACAO] 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'LFS_00'
             AND   COLUMNS.COLUMN_NAME = 'LFS_INTEGRACAO')
 BEGIN 
    IF NOT EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
                    WHERE COLUMNS.TABLE_NAME = 'LFS_00'
                    AND COLUMNS.COLUMN_NAME  = 'LFS_INTEGRACAO_OLD')
    BEGIN 
       ALTER TABLE dbo.LFS_00 ADD LFS_INTEGRACAO_OLD VARCHAR(50) NULL 
    END; 
 END; 
 GO 
  
 -- Guarda [LFS_INTEGRACAO] de integrações no novo [LFS_INTEGRACAO_OLD] bkp 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'LFS_00'
             AND   COLUMNS.COLUMN_NAME = 'LFS_INTEGRACAO')
 BEGIN 
    UPDATE LFS_00
    SET    [LFS_INTEGRACAO_OLD] = LFS_INTEGRACAO
    WHERE  [LFS_INTEGRACAO_OLD] IS NULL 
 END; 
 GO 
  
 -- Executa o SEQ_LFS_ID pra gerar os novos IDs do campo [LFS_ID] 
 UPDATE LFS_00
 SET    LFS_ID = NEXT VALUE FOR SEQ_LFS_ID
 GO 
  
 -- Recria índice Primary Key da table  [LFS_ID] para o campo [LFS_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'LFS_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [LFS_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'LFS_00_PK')
             ALTER TABLE dbo.LFS_00
             DROP CONSTRAINT LFS_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.LFS_00
 ADD CONSTRAINT LFS_00_pk 
 PRIMARY KEY CLUSTERED (LFS_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [LFS_00] 
 REBUILD 
 END
 GO
  
