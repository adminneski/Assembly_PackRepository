 -- Cria o SEQUENCE para o campo [TPO_ID] da table [TPO_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_TPO_ID'))
  CREATE SEQUENCE dbo.SEQ_TPO_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [TPO_ID_OLD] pra receber IDs do campo [TPO_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'TPO_00'
 AND COLUMNS.COLUMN_NAME = 'TPO_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.TPO_00
 ADD TPO_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [TPO_ID] de relação com o cabeçalho no novo [TPO_ID_OLD] bkp 
 UPDATE TPO_00
 SET    TPO_ID_OLD = TPO_ID
 WHERE  TPO_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_TPO_ID pra gerar os novos IDs do campo [TPO_ID] 
 UPDATE TPO_00
 SET    TPO_ID = NEXT VALUE FOR SEQ_TPO_ID
 GO 
  
 -- Recria índice Primary Key da table  [TPO_ID] para o campo [TPO_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'TPO_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [TPO_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'TPO_00_PK')
             ALTER TABLE dbo.TPO_00
             DROP CONSTRAINT TPO_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.TPO_00
 ADD CONSTRAINT TPO_00_pk 
 PRIMARY KEY CLUSTERED (TPO_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [TPO_00] 
 REBUILD 
 END
 GO
  
