 -- Cria o SEQUENCE para o campo [SCT_ID] da table [SCT_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_SCT_ID'))
  CREATE SEQUENCE dbo.SEQ_SCT_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [SCT_ID_OLD] pra receber IDs do campo [SCT_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'SCT_00'
 AND COLUMNS.COLUMN_NAME = 'SCT_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.SCT_00
 ADD SCT_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [SCT_ID] de relação com o cabeçalho no novo [SCT_ID_OLD] bkp 
 UPDATE SCT_00
 SET    SCT_ID_OLD = SCT_ID
 WHERE  SCT_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_SCT_ID pra gerar os novos IDs do campo [SCT_ID] 
 UPDATE SCT_00
 SET    SCT_ID = NEXT VALUE FOR SEQ_SCT_ID
 GO 
  
 -- Recria índice Primary Key da table  [SCT_ID] para o campo [SCT_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'SCT_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [SCT_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'PK_SCT_00')
             ALTER TABLE dbo.SCT_00
             DROP CONSTRAINT PK_SCT_00
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.SCT_00
 ADD CONSTRAINT SCT_00_pk 
 PRIMARY KEY CLUSTERED (SCT_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [SCT_00] 
 REBUILD 
 END
 GO
  
