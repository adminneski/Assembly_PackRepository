 -- Cria o SEQUENCE para o campo [FLP_ID] da table [FLP_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_FLP_ID'))
  CREATE SEQUENCE dbo.SEQ_FLP_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [FLP_ID_OLD] pra receber IDs do campo [FLP_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'FLP_00'
 AND COLUMNS.COLUMN_NAME = 'FLP_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.FLP_00
 ADD FLP_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [FLP_ID] de relação com o cabeçalho no novo [FLP_ID_OLD] bkp 
 UPDATE FLP_00
 SET    FLP_ID_OLD = FLP_ID
 WHERE  FLP_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_FLP_ID pra gerar os novos IDs do campo [FLP_ID] 
 UPDATE FLP_00
 SET    FLP_ID = NEXT VALUE FOR SEQ_FLP_ID
 GO 
  
 -- Recria índice Primary Key da table  [FLP_ID] para o campo [FLP_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'FLP_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [FLP_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'FLP_00_PK')
             ALTER TABLE dbo.FLP_00
             DROP CONSTRAINT FLP_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.FLP_00
 ADD CONSTRAINT FLP_00_pk 
 PRIMARY KEY CLUSTERED (FLP_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [FLP_00] 
 REBUILD 
 END
 GO
  
