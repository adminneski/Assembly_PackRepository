 -- Cria o SEQUENCE para o campo [FLP_01_ID] da table [FLP_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_FLP_01_ID'))
  CREATE SEQUENCE dbo.SEQ_FLP_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [FLP_01_ID_OLD] pra receber IDs do campo [FLP_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'FLP_00_01'
 AND COLUMNS.COLUMN_NAME = 'FLP_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.FLP_00_01
 ADD FLP_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [FLP_ID_OLD] pra receber IDs do campo [FLP_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'FLP_00_01'
 AND COLUMNS.COLUMN_NAME = 'FLP_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.FLP_00_01
 ADD FLP_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [FLP_01_ID] de relação com o cabeçalho no novo [FLP_01_ID_OLD] bkp 
 UPDATE FLP_00_01
 SET    FLP_01_ID_OLD = FLP_01_ID
 WHERE  FLP_01_ID_OLD IS NULL 
 GO 
  
 -- Guarda [FLP_ID] de relação com o cabeçalho no novo [FLP_ID_OLD] bkp 
 UPDATE FLP_00_01
 SET    FLP_ID_OLD = FLP_ID
 WHERE  FLP_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_FLP_01_ID pra gerar os novos IDs do campo [FLP_01_ID] 
 UPDATE FLP_00_01
 SET    FLP_01_ID = NEXT VALUE FOR SEQ_FLP_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [FLP_01_ID] para o campo [FLP_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'FLP_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [FLP_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'FLP_00_01_PK')
             ALTER TABLE dbo.FLP_00_01
             DROP CONSTRAINT FLP_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.FLP_00_01
 ADD CONSTRAINT FLP_00_01_pk 
 PRIMARY KEY CLUSTERED (FLP_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [FLP_00_01] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [FLP_00] campo [FLP_ID], na table filha [FLP_00_01] campo [FLP_01_ID]
 UPDATE FLP_00_01 -- FILHA
 SET FLP_00_01.FLP_ID = T2.FLP_ID -- FILHA  RECEBE DA MÃE
 FROM FLP_00_01 -- FILHA
 INNER JOIN FLP_00 T2 ON (FLP_00_01.FLP_ID_OLD = T2.FLP_ID_OLD)
 AND (FLP_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
