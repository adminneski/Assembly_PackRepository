 -- Cria o SEQUENCE para o campo [CPA_01_ID] da table [CPA_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CPF_01_ID'))
  CREATE SEQUENCE dbo.SEQ_CPF_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [CPF_01_ID_OLD] pra receber IDs do campo [CPF_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CPF_00_01'
 AND COLUMNS.COLUMN_NAME = 'CPF_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CPF_00_01
 ADD CPF_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [CPF_ID_OLD] pra receber IDs do campo [CPF_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CPF_00_01'
 AND COLUMNS.COLUMN_NAME = 'CPF_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CPF_00_01
 ADD CPF_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [CPF_01_ID] de relação com o cabeçalho no novo [CPF_01_ID_OLD] bkp 
 UPDATE CPF_00_01
 SET    CPF_01_ID_OLD = CPF_01_ID,
 CPF_ID_OLD = CPF_ID
 WHERE  CPF_01_ID_OLD IS NULL 
 GO
  
 -- Executa o SEQ_CPA_01_ID pra gerar os novos IDs do campo [CPA_01_ID] 
 UPDATE CPF_00_01
 SET    CPF_01_ID = NEXT VALUE FOR SEQ_CPF_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [CPA_01_ID] para o campo [CPA_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CPF_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CPF_00_01]
 DISABLE
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CPF_00_01_PK')
             ALTER TABLE dbo.CPF_00_01
             DROP CONSTRAINT CPF_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CPF_00_01
 ADD CONSTRAINT CPF_00_01_pk 
 PRIMARY KEY CLUSTERED (CPF_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CPF_00_01] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [CPA_00] campo [CPA_ID], na table filha [CPA_00_01] campo [CPA_01_ID]
 UPDATE CPF_00_01 -- FILHA
 SET CPF_00_01.CPF_ID = T2.CPF_ID -- FILHA  RECEBE DA MÃE
 FROM CPF_00_01 -- FILHA
 INNER JOIN CPF_00 T2 ON (CPF_00_01.CPF_ID_OLD = T2.CPF_ID_OLD)
 AND (CPF_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO