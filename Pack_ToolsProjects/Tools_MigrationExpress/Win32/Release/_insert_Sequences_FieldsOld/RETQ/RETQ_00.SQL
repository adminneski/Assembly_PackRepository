 -- Cria o SEQUENCE para o campo [RETQ_ID] da table [RETQ_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_RETQ_ID'))
  CREATE SEQUENCE dbo.SEQ_RETQ_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE
  CACHE 
  GO 
  
 -- "1115" Criando campo [RETQ_ID_OLD] pra receber IDs do campo [RETQ_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'RETQ_00'
 AND COLUMNS.COLUMN_NAME = 'RETQ_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.RETQ_00
 ADD RETQ_ID_OLD int NULL 
 END 
 GO

 -- "1267" Guarda [RETQ_ID_OLD] de relação com o cabeçalho no novo [RETQ_ID] bkp 
 UPDATE RETQ_00
 SET RETQ_ID_OLD = RETQ_ID
 WHERE  RETQ_ID_OLD IS NULL 
 GO 
 
 -- Executa o SEQ_RETQ_ID pra gerar os novos IDs do campo [RETQ_ID] 
 UPDATE RETQ_00
 SET    RETQ_ID = NEXT VALUE FOR SEQ_RETQ_ID
 GO 
  
 -- Recria índice Primary Key da table  [RETQ_00] para o campo [RETQ_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'RETQ_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [RETQ_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'RETQ_00_pk')
             ALTER TABLE dbo.RETQ_00
             DROP CONSTRAINT RETQ_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.RETQ_00
