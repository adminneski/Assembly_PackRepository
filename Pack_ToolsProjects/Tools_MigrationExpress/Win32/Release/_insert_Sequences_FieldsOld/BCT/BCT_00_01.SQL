 -- Cria o SEQUENCE para o campo [BCT_01_ID] da table [BCT_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_BCT_01_ID'))
  CREATE SEQUENCE dbo.SEQ_BCT_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [BCT_ID_OLD] pra receber IDs do campo [BCT_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'BCT_00_01'
 AND COLUMNS.COLUMN_NAME = 'BCT_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.BCT_00_01
 ADD BCT_ID_OLD int NULL 
 END 
 GO 

  -- Criando campo [BCT_ID_OLD] pra receber IDs do campo [BCT_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'BCT_00_01'
 AND COLUMNS.COLUMN_NAME = 'BCT_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.BCT_00_01
 ADD BCT_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [BCT_01_ID] de relação com o cabeçalho no novo [BCT_01_ID_OLD] bkp 
 UPDATE BCT_00_01
 SET    BCT_01_ID_OLD = BCT_01_ID
 WHERE  BCT_01_ID_OLD IS NULL 
 GO 
  
 -- Guarda [BCT_ID] de relação com o cabeçalho no novo [BCT_ID_OLD] bkp 
 UPDATE BCT_00_01
 SET    BCT_ID_OLD = BCT_ID
 WHERE  BCT_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_BCT_01_ID pra gerar os novos IDs do campo [BCT_01_ID] 
 UPDATE BCT_00_01
 SET    BCT_01_ID = NEXT VALUE FOR SEQ_BCT_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [BCT_01_ID] para o campo [BCT_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'BCT_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [BCT_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'BCT_00_01_PK')
             ALTER TABLE dbo.BCT_00_01
             DROP CONSTRAINT BCT_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.BCT_00_01
 ADD CONSTRAINT BCT_00_01_pk 
 PRIMARY KEY CLUSTERED (BCT_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [BCT_00_01] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [BCT_00] campo [BCT_ID], na table filha [BCT_00_01] campo [BCT_01_ID]
 UPDATE BCT_00_01 -- FILHA
 SET BCT_00_01.BCT_ID = T2.BCT_ID -- FILHA  RECEBE DA MÃE
 FROM BCT_00_01 -- FILHA
 INNER JOIN BCT_00 T2 ON (BCT_00_01.BCT_ID_OLD = T2.BCT_ID_OLD)
 AND (BCT_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
