 -- Cria o SEQUENCE para o campo [VEF_ID] da table [VEF_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_VEF_ID'))
  CREATE SEQUENCE dbo.SEQ_VEF_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [VEF_ID_OLD] pra receber IDs do campo [VEF_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'VEF_00'
 AND COLUMNS.COLUMN_NAME = 'VEF_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.VEF_00
 ADD VEF_ID_OLD int NULL 
 END; 
 GO 

  
 -- "MOTHER" Guarda [VEF_ID] de relação com o cabeçalho no novo [VEF_ID_OLD] bkp 
 UPDATE VEF_00
 SET    VEF_ID_OLD = VEF_ID
 WHERE  VEF_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_VEF_ID pra gerar os novos IDs do campo [VEF_ID] 
 UPDATE VEF_00
 SET    VEF_ID = NEXT VALUE FOR SEQ_VEF_ID
 GO 
  
 -- Recria índice Primary Key da table  [VEF_ID] para o campo [VEF_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'VEF_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [VEF_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'VEF_00_PK')
             ALTER TABLE dbo.VEF_00
             DROP CONSTRAINT VEF_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.VEF_00
 ADD CONSTRAINT VEF_00_pk 
 PRIMARY KEY CLUSTERED (VEF_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [VEF_00] 
 REBUILD 
 END
 GO
  
