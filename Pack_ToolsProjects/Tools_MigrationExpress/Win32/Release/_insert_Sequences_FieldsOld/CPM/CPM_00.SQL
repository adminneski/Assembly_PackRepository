 -- Cria o SEQUENCE para o campo [CPM_ID] da table [CPM_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CPM_ID'))
  CREATE SEQUENCE dbo.SEQ_CPM_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [CPM_ID_OLD] pra receber IDs do campo [CPM_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CPM_00'
 AND COLUMNS.COLUMN_NAME = 'CPM_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CPM_00
 ADD CPM_ID_OLD int NULL 
 END; 
 GO 
 
  
 -- Guarda [CPM_ID] de relação com o cabeçalho no novo [CPM_ID_OLD] bkp 
 UPDATE CPM_00
 SET    CPM_ID_OLD = CPM_ID
 WHERE  CPM_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_CPM_ID pra gerar os novos IDs do campo [CPM_ID] 
 UPDATE CPM_00
 SET    CPM_ID = NEXT VALUE FOR SEQ_CPM_ID
 GO 
  
 -- Recria índice Primary Key da table  [CPM_ID] para o campo [CPM_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CPM_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CPM_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CPM_00_PK')
             ALTER TABLE dbo.CPM_00
             DROP CONSTRAINT CPM_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CPM_00
 ADD CONSTRAINT CPM_00_pk 
 PRIMARY KEY CLUSTERED (CPM_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CPM_00] 
 REBUILD 
 END
 GO
  
