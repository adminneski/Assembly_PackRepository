 -- Cria o SEQUENCE para o campo [BCP_ID] da table [BCP_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_BCP_ID'))
  CREATE SEQUENCE dbo.SEQ_BCP_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1408" Criando campo [BCP_ID_OLD] pra receber IDs do campo [BCP_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'BCP_00'
 AND COLUMNS.COLUMN_NAME = 'BCP_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.BCP_00
 ADD BCP_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [BCP_ID] de relação com o cabeçalho no novo [BCP_ID_OLD] bkp 
 UPDATE BCP_00
 SET    BCP_ID_OLD = BCP_ID
 WHERE  BCP_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_BCP_ID pra gerar os novos IDs do campo [BCP_ID] 
 UPDATE BCP_00
 SET    BCP_ID = NEXT VALUE FOR SEQ_BCP_ID
 GO 
  
 -- Recria índice Primary Key da table  [BCP_ID] para o campo [BCP_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'BCP_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [BCP_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'BCP_00_PK')
             ALTER TABLE dbo.BCP_00
             DROP CONSTRAINT BCP_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.BCP_00
 ADD CONSTRAINT BCP_00_pk 
 PRIMARY KEY CLUSTERED (BCP_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [BCP_00] 
 REBUILD 
 END
  
