 -- Cria o SEQUENCE para o campo [XML_ID] da table [XML_NFSE_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_XML_NFSE_ID'))
  CREATE SEQUENCE dbo.SEQ_XML_NFSE_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1115" Criando campo [XML_ID_OLD] pra receber IDs do campo [XML_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'XML_NFSE_00'
 AND COLUMNS.COLUMN_NAME = 'XML_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.XML_NFSE_00
 ADD XML_ID_OLD int NULL 
 END 
 GO 
  
 -- "INTEGRACÃO" Criando campo [XML_INTEGRACAO_OLD] pra receber BKP DAS INTEGRAÇÕES do campo [XML_INTEGRACAO] 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'XML_NFSE_00'
             AND   COLUMNS.COLUMN_NAME = 'XML_INTEGRACAO')
 BEGIN 
    IF NOT EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
                    WHERE COLUMNS.TABLE_NAME = 'XML_NFSE_00'
                    AND COLUMNS.COLUMN_NAME  = 'XML_INTEGRACAO_OLD')
    BEGIN 
       ALTER TABLE dbo.XML_NFSE_00 ADD XML_INTEGRACAO_OLD VARCHAR(50) NULL 
    END; 
 END; 
 GO 
  
 -- "INTEGRACÃO" Guarda [XML_INTEGRACAO] de integrações no novo [XML_INTEGRACAO_OLD] bkp 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'XML_NFSE_00'
             AND   COLUMNS.COLUMN_NAME = 'XML_INTEGRACAO')
 BEGIN 
    UPDATE XML_NFSE_00
    SET    [XML_INTEGRACAO_OLD] = XML_INTEGRACAO
    WHERE  [XML_INTEGRACAO_OLD] IS NULL 
 END; 
 GO 

-- "1267" Guarda [XML_ID] de relação com o cabeçalho no novo [XML_ID_OLD] bkp 
 UPDATE XML_NFSE_00
 SET    XML_ID_OLD = XML_ID
 WHERE  XML_ID_OLD IS NULL 
 GO
  
 -- Executa o SEQ_XML_NFSE_IDpra gerar os novos IDs do campo [XML_ID] 
 UPDATE XML_NFSE_00
 SET    XML_ID = NEXT VALUE FOR SEQ_XML_NFSE_ID
 GO 
  
 -- Recria índice Primary Key da table [XML_NFSE_00] para o campo [XML_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'XML_NFSE_00'
    HAVING COUNT(*) >= 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [XML_NFSE_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'XML_NFSE_00_pk')
             ALTER TABLE dbo.XML_NFSE_00
             DROP CONSTRAINT XML_NFSE_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.XML_NFSE_00
 ADD CONSTRAINT XML_NFSE_00_pk 
 PRIMARY KEY CLUSTERED (XML_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [XML_NFSE_00] 
 REBUILD 
 END
 GO