 -- Cria o SEQUENCE para o campo [PDC_ID] da table [PDC_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_PDC_ID'))
  CREATE SEQUENCE dbo.SEQ_PDC_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [PDC_ID_OLD] pra receber IDs do campo [PDC_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'PDC_00'
 AND COLUMNS.COLUMN_NAME = 'PDC_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.PDC_00
 ADD PDC_ID_OLD int NULL 
 END; 
 GO 

  
 -- "MOTHER" Guarda [PDC_ID] de relação com o cabeçalho no novo [PDC_ID_OLD] bkp 
 UPDATE PDC_00
 SET    PDC_ID_OLD = PDC_ID
 WHERE  PDC_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_PDC_ID pra gerar os novos IDs do campo [PDC_ID] 
 UPDATE PDC_00
 SET    PDC_ID = NEXT VALUE FOR SEQ_PDC_ID
 GO 
  
 -- Recria índice Primary Key da table  [PDC_ID] para o campo [PDC_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'PDC_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [PDC_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'PDC_00_PK')
             ALTER TABLE dbo.PDC_00
             DROP CONSTRAINT PDC_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.PDC_00
 ADD CONSTRAINT PDC_00_pk 
 PRIMARY KEY CLUSTERED (PDC_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [PDC_00] 
 REBUILD 
 END
 GO
  
