 -- Cria o SEQUENCE para o campo [DCH_01_ID] da table [DCH_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_DCH_01_ID'))
  CREATE SEQUENCE dbo.SEQ_DCH_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [DCH_01_ID_OLD] pra receber IDs do campo [DCH_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'DCH_00_01'
 AND COLUMNS.COLUMN_NAME = 'DCH_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.DCH_00_01
 ADD DCH_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [DCH_ID_OLD] pra receber IDs do campo [DCH_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'DCH_00_01'
 AND COLUMNS.COLUMN_NAME = 'DCH_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.DCH_00_01
 ADD DCH_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [DCH_01_ID] de relação com o cabeçalho no novo [DCH_01_ID_OLD] bkp 
 UPDATE DCH_00_01
 SET    DCH_01_ID_OLD = DCH_01_ID
 WHERE  DCH_01_ID_OLD IS NULL 
 GO 
  
 -- Guarda [DCH_ID] de relação com o cabeçalho no novo [DCH_ID_OLD] bkp 
 UPDATE DCH_00_01
 SET    DCH_ID_OLD = DCH_ID
 WHERE  DCH_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_DCH_01_ID pra gerar os novos IDs do campo [DCH_01_ID] 
 UPDATE DCH_00_01
 SET    DCH_01_ID = NEXT VALUE FOR SEQ_DCH_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [DCH_01_ID] para o campo [DCH_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'DCH_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [DCH_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'DCH_00_01_PK')
             ALTER TABLE dbo.DCH_00_01
             DROP CONSTRAINT DCH_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.DCH_00_01
 ADD CONSTRAINT DCH_00_01_pk 
 PRIMARY KEY CLUSTERED (DCH_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [DCH_00_01] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [DCH_00] campo [DCH_ID], na table filha [DCH_00_01] campo [DCH_01_ID]
 UPDATE DCH_00_01 -- FILHA
 SET DCH_00_01.DCH_ID = T2.DCH_ID -- FILHA  RECEBE DA MÃE
 FROM DCH_00_01 -- FILHA
 INNER JOIN DCH_00 T2 ON (DCH_00_01.DCH_ID_OLD = T2.DCH_ID_OLD)
 AND (DCH_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
