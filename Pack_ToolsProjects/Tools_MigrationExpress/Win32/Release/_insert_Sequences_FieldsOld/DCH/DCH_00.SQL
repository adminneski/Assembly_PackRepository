 -- Cria o SEQUENCE para o campo [DCH_ID] da table [DCH_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_DCH_ID'))
  CREATE SEQUENCE dbo.SEQ_DCH_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [DCH_ID_OLD] pra receber IDs do campo [DCH_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'DCH_00'
 AND COLUMNS.COLUMN_NAME = 'DCH_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.DCH_00
 ADD DCH_ID_OLD int NULL 
 END; 
 GO 
  
 -- Guarda [DCH_ID] de relação com o cabeçalho no novo [DCH_ID_OLD] bkp 
 UPDATE DCH_00
 SET    DCH_ID_OLD = DCH_ID
 WHERE  DCH_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_DCH_ID pra gerar os novos IDs do campo [DCH_ID] 
 UPDATE DCH_00
 SET    DCH_ID = NEXT VALUE FOR SEQ_DCH_ID
 GO 
  
 -- Recria índice Primary Key da table  [DCH_ID] para o campo [DCH_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'DCH_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [DCH_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'DCH_00_PK')
             ALTER TABLE dbo.DCH_00
             DROP CONSTRAINT DCH_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.DCH_00
 ADD CONSTRAINT DCH_00_pk 
 PRIMARY KEY CLUSTERED (DCH_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [DCH_00] 
 REBUILD 
 END
 GO
  
