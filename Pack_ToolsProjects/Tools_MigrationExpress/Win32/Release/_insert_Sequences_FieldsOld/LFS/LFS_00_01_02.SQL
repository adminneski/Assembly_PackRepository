 -- Cria o SEQUENCE para o campo [LFS_01_02_ID] da table [LFS_00_01_02]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_LFS_01_02_ID'))
  CREATE SEQUENCE dbo.SEQ_LFS_01_02_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [LFS_ID_OLD] pra receber IDs do campo [LFS_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'LFS_00_01_02'
 AND COLUMNS.COLUMN_NAME = 'LFS_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.LFS_00_01_02
 ADD LFS_ID_OLD int NULL 
 END; 
 GO 
  
 -- Criando campo [LFS_01_ID_OLD] pra receber IDs do campo [LFS_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'LFS_00_01_02'
 AND COLUMNS.COLUMN_NAME = 'LFS_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.LFS_00_01_02
 ADD LFS_01_ID_OLD int NULL 
 END; 
 GO 
  
 -- Guarda [LFS_ID_OLD] e [LFS_01_ID_OLD] de relação com o cabeçalho no novo field [_OLD] 
 UPDATE LFS_00_01_02
 SET LFS_ID_OLD = LFS_ID
 ,LFS_01_ID_OLD = LFS_01_ID
 WHERE LFS_ID_OLD IS NULL 
 GO 

 -- Executa o SEQ_LFS_01_02_ID pra gerar os novos IDs do campo [LFS_01_02_ID] 
 UPDATE LFS_00_01_02
 SET    LFS_01_02_ID = NEXT VALUE FOR SEQ_LFS_01_02_ID
 GO 
  
 -- Recria índice Primary Key da table  [LFS_01_02_ID] para o campo [LFS_01_02_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'LFS_00_01_02'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [LFS_00_01_02]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'LFS_00_01_02_PK')
             ALTER TABLE dbo.LFS_00_01_02
             DROP CONSTRAINT LFS_00_01_02_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.LFS_00_01_02
 ADD CONSTRAINT LFS_00_01_02_pk 
 PRIMARY KEY CLUSTERED (LFS_01_02_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [LFS_00_01_02] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [LFS_00_01] campo [LFS_ID], na table filha [LFS_00_01_02] campo [LFS_01_02_ID]
 UPDATE LFS_00_01_02 -- FILHA
 SET LFS_00_01_02.LFS_ID = T2.LFS_ID -- FILHA  RECEBE DA MÃE
 , LFS_00_01_02.LFS_01_ID = T2.LFS_01_ID -- FILHA  RECEBE DA MÃE
 FROM LFS_00_01_02 -- FILHA
 INNER JOIN LFS_00_01 T2 ON (LFS_00_01_02.LFS_01_ID_OLD = T2.LFS_01_ID_OLD)
