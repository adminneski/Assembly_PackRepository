 -- Cria o SEQUENCE para o campo [CDV_01_ID] da table [CDV_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CDV_01_ID'))
  CREATE SEQUENCE dbo.SEQ_CDV_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [CDV_01_ID_OLD] pra receber IDs do campo [CDV_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CDV_00_01'
 AND COLUMNS.COLUMN_NAME = 'CDV_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CDV_00_01
 ADD CDV_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [CDV_ID_OLD] pra receber IDs do campo [CDV_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CDV_00_01'
 AND COLUMNS.COLUMN_NAME = 'CDV_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CDV_00_01
 ADD CDV_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [CDV_01_ID] de relação com o cabeçalho no novo [CDV_01_ID_OLD] bkp 
 UPDATE CDV_00_01
 SET    CDV_01_ID_OLD = CDV_01_ID
 WHERE  CDV_01_ID_OLD IS NULL 
 GO 
  
 -- Guarda [CDV_ID] de relação com o cabeçalho no novo [CDV_ID_OLD] bkp 
 UPDATE CDV_00_01
 SET    CDV_ID_OLD = CDV_ID
 WHERE  CDV_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_CDV_01_ID pra gerar os novos IDs do campo [CDV_01_ID] 
 UPDATE CDV_00_01
 SET    CDV_01_ID = NEXT VALUE FOR SEQ_CDV_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [CDV_01_ID] para o campo [CDV_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CDV_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CDV_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CDV_00_01_PK')
             ALTER TABLE dbo.CDV_00_01
             DROP CONSTRAINT CDV_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CDV_00_01
 ADD CONSTRAINT CDV_00_01_pk 
 PRIMARY KEY CLUSTERED (CDV_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CDV_00_01] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [CDV_00] campo [CDV_ID], na table filha [CDV_00_01] campo [CDV_01_ID]
 UPDATE CDV_00_01 -- FILHA
 SET CDV_00_01.CDV_ID = T2.CDV_ID -- FILHA  RECEBE DA MÃE
 FROM CDV_00_01 -- FILHA
 INNER JOIN CDV_00 T2 ON (CDV_00_01.CDV_ID_OLD = T2.CDV_ID_OLD)
 AND (CDV_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
