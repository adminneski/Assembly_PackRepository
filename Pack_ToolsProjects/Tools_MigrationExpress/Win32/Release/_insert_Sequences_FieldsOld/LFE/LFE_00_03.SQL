 -- Cria o SEQUENCE para o campo [LFE_03_ID] da table [LFE_00_03]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_LFE_03_ID'))
  CREATE SEQUENCE dbo.SEQ_LFE_03_ID
  AS int
  START WITH 1
  NO MINVALUE
  NO MAXVALUE
  NO CYCLE
  CACHE
  GO

 -- Criando campo [LFE_03_ID_OLD] pra receber IDs do campo [LFE_03_ID] PARA GUARDAR OS IDs ANTIGOS
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS
 WHERE COLUMNS.TABLE_NAME = 'LFE_00_03'
 AND COLUMNS.COLUMN_NAME = 'LFE_03_ID_OLD')
 BEGIN
 ALTER TABLE dbo.LFE_00_03
 ADD LFE_03_ID_OLD int NULL
 END
 GO

 -- Criando campo [LFE_ID_OLD] pra receber IDs do campo [LFE_ID] PARA GUARDAR OS IDs ANTIGOS
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS
 WHERE COLUMNS.TABLE_NAME = 'LFE_00_03'
 AND COLUMNS.COLUMN_NAME = 'LFE_ID_OLD')
 BEGIN
 ALTER TABLE dbo.LFE_00_03
 ADD LFE_ID_OLD int NULL
 END
 GO

 -- Guarda [LFE_03_ID] de relação com o cabeçalho no novo [LFE_03_ID_OLD] bkp
 UPDATE LFE_00_03
 SET    LFE_03_ID_OLD = LFE_03_ID,
 LFE_ID_OLD = LFE_ID
 WHERE  LFE_03_ID_OLD IS NULL
 GO

 -- Executa o SEQ_LFE_03_ID pra gerar os novos IDs do campo [LFE_03_ID]
 UPDATE LFE_00_03
 SET    LFE_03_ID = NEXT VALUE FOR SEQ_LFE_03_ID
 GO

-- Recria índice Primary Key da table  [LFE_03_ID] para o campo [LFE_03_ID]
 IF EXISTS ( SELECT count(table_name)
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE
    WHERE table_name = 'LFE_00_03'
    HAVING COUNT(*) > 1 )
 BEGIN
 -- DESABILITA INDICE
 ALTER INDEX ALL ON [LFE_00_03]
 DISABLE

 -- VERIFICA SE EXISTE O INDICE E O APAGA
 IF  EXISTS (SELECT *
             FROM sys.indexes AS si
             WHERE si.name = 'LFE_00_03_IDX')
             ALTER TABLE dbo.LFE_00_03
             DROP CONSTRAINT LFE_00_03_idx

 -- ALTERA A TABLE CRIANDO O INDICE
 ALTER TABLE dbo.LFE_00_03
 ADD CONSTRAINT LFE_00_03_PK
 PRIMARY KEY CLUSTERED (LFE_03_ID)
 WITH (
   PAD_INDEX = OFF,
   FILLFACTOR = 90,
   IGNORE_DUP_KEY = OFF,
   STATISTICS_NORECOMPUTE = OFF,
   ALLOW_ROW_LOCKS = ON,
   ALLOW_PAGE_LOCKS = ON)
 ON [PRIMARY]

 -- REABILITA INDICES
 ALTER INDEX ALL ON [LFE_00_03]
 REBUILD
 END
 GO

-- Atualiza novos ids da mãe [LFE_00] campo [LFE_ID], na table filha [LFE_00_03] campo [LFE_03_ID]
 UPDATE LFE_00_03 -- FILHA
 SET LFE_00_03.LFE_ID = T2.LFE_ID -- FILHA  RECEBE DA MÃE
 FROM LFE_00_03 -- FILHA
 INNER JOIN LFE_00 T2 ON (LFE_00_03.LFE_ID_OLD = T2.LFE_ID_OLD)
 AND (LFE_00_03.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO