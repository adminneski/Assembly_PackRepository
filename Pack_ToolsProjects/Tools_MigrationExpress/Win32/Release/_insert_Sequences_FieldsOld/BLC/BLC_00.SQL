 -- Cria o SEQUENCE para o campo [BLC_ID] da table [BLC_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_BLC_ID'))
  CREATE SEQUENCE dbo.SEQ_BLC_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [BLC_ID_OLD] pra receber IDs do campo [BLC_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'BLC_00'
 AND COLUMNS.COLUMN_NAME = 'BLC_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.BLC_00
 ADD BLC_ID_OLD int NULL 
 END; 
 GO 
  
 -- Criando campo [BLC_ID_OLD] pra receber IDs do campo [BLC_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'BLC_00'
 AND COLUMNS.COLUMN_NAME = 'BLC_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.BLC_00
 ADD BLC_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [BLC_ID] de relação com o cabeçalho no novo [BLC_ID_OLD] bkp 
 UPDATE BLC_00
 SET    BLC_ID_OLD = BLC_ID
 WHERE  BLC_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_BLC_ID pra gerar os novos IDs do campo [BLC_ID] 
 UPDATE BLC_00
 SET    BLC_ID = NEXT VALUE FOR SEQ_BLC_ID
 GO 
  
 -- Recria índice Primary Key da table  [BLC_ID] para o campo [BLC_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'BLC_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [BLC_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'BLC_00_PK')
             ALTER TABLE dbo.BLC_00
             DROP CONSTRAINT BLC_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.BLC_00
 ADD CONSTRAINT BLC_00_pk 
 PRIMARY KEY CLUSTERED (BLC_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [BLC_00] 
 REBUILD 
 END
 GO
  
