 -- Cria o SEQUENCE para o campo [FAT_02_ID] da table [FAT_00_02]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_FAT_02_ID'))
  CREATE SEQUENCE dbo.SEQ_FAT_02_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [FAT_02_ID_OLD] pra receber IDs do campo [FAT_02_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'FAT_00_02'
 AND COLUMNS.COLUMN_NAME = 'FAT_02_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.FAT_00_02
 ADD FAT_02_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [FAT_ID_OLD] pra receber IDs do campo [FAT_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'FAT_00_02'
 AND COLUMNS.COLUMN_NAME = 'FAT_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.FAT_00_02
 ADD FAT_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [FAT_02_ID] de relação com o cabeçalho no novo [FAT_02_ID_OLD] bkp 
 UPDATE FAT_00_02
 SET    FAT_02_ID_OLD = FAT_02_ID
 WHERE  FAT_02_ID_OLD IS NULL 
 GO 
  
 -- Guarda [FAT_ID] de relação com o cabeçalho no novo [FAT_ID_OLD] bkp 
 UPDATE FAT_00_02
 SET    FAT_ID_OLD = FAT_ID
 WHERE  FAT_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_FAT_02_ID pra gerar os novos IDs do campo [FAT_02_ID] 
 UPDATE FAT_00_02
 SET    FAT_02_ID = NEXT VALUE FOR SEQ_FAT_02_ID
 GO 
  
 -- Recria índice Primary Key da table  [FAT_02_ID] para o campo [FAT_02_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'FAT_00_02'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [FAT_00_02]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'FAT_00_02_PK')
             ALTER TABLE dbo.FAT_00_02
             DROP CONSTRAINT FAT_00_02_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.FAT_00_02
 ADD CONSTRAINT FAT_00_02_pk 
 PRIMARY KEY CLUSTERED (FAT_02_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [FAT_00_02] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [FAT_00] campo [FAT_ID], na table filha [FAT_00_02] campo [FAT_02_ID]
 UPDATE FAT_00_02 -- FILHA
 SET FAT_00_02.FAT_ID = T2.FAT_ID -- FILHA  RECEBE DA MÃE
 FROM FAT_00_02 -- FILHA
 INNER JOIN FAT_00 T2 ON (FAT_00_02.FAT_ID_OLD = T2.FAT_ID_OLD)
 AND (FAT_00_02.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
