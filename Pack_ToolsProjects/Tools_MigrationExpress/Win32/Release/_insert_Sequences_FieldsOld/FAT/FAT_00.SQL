 -- Cria o SEQUENCE para o campo [FAT_ID] da table [FAT_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_FAT_ID'))
  CREATE SEQUENCE dbo.SEQ_FAT_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [FAT_ID_OLD] pra receber IDs do campo [FAT_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'FAT_00'
 AND COLUMNS.COLUMN_NAME = 'FAT_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.FAT_00
 ADD FAT_ID_OLD int NULL 
 END; 
 GO

 -- Guarda [FAT_ID] de relação com o cabeçalho no novo [FAT_ID_OLD] bkp 
 UPDATE FAT_00
 SET    FAT_ID_OLD = FAT_ID
 WHERE  FAT_ID_OLD IS NULL 
 GO 
  
 -- Criando campo [PDA_ID_OLD] pra receber IDs do campo [PDA_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'FAT_00'
 AND COLUMNS.COLUMN_NAME = 'PDA_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.FAT_00
 ADD PDA_ID_OLD int NULL 
 END; 
 GO

 -- Guarda [FAT_ID] de relação com o cabeçalho no novo [FAT_ID_OLD] bkp 
 UPDATE FAT_00
 SET    PDA_ID_OLD = PDA_ID
 WHERE  PDA_ID_OLD IS NULL 
 GO 

 -- "INTEGRACÃO" Criando campo [FAT_INTEGRACAO_OLD] pra receber BKP DAS INTEGRAÇÕES do campo [FAT_INTEGRACAO] 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'FAT_00'
             AND   COLUMNS.COLUMN_NAME = 'FAT_INTEGRACAO')
 BEGIN 
    IF NOT EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
                    WHERE COLUMNS.TABLE_NAME = 'FAT_00'
                    AND COLUMNS.COLUMN_NAME  = 'FAT_INTEGRACAO_OLD')
    BEGIN 
       ALTER TABLE dbo.FAT_00 ADD FAT_INTEGRACAO_OLD VARCHAR(50) NULL 
    END; 
 END; 
 GO 
  
 -- "INTEGRACÃO" Guarda [FAT_INTEGRACAO] de integrações no novo [FAT_INTEGRACAO_OLD] bkp 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'FAT_00'
             AND   COLUMNS.COLUMN_NAME = 'FAT_INTEGRACAO')
 BEGIN 
    UPDATE FAT_00
    SET    [FAT_INTEGRACAO_OLD] = FAT_INTEGRACAO
    WHERE  [FAT_INTEGRACAO_OLD] IS NULL 
 END; 
 GO 
  
 -- Executa o SEQ_FAT_ID pra gerar os novos IDs do campo [FAT_ID] 
 UPDATE FAT_00
 SET    FAT_ID = NEXT VALUE FOR SEQ_FAT_ID
 GO 
  
 -- Recria índice Primary Key da table  [FAT_ID] para o campo [FAT_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'FAT_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [FAT_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'FAT_00_PK')
             ALTER TABLE dbo.FAT_00
             DROP CONSTRAINT FAT_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.FAT_00
 ADD CONSTRAINT FAT_00_pk 
 PRIMARY KEY CLUSTERED (FAT_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [FAT_00] 
 REBUILD 
 END
 GO
  
