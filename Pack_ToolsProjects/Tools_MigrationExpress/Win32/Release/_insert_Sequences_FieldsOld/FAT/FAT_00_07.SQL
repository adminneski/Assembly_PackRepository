 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'FAT_00_07'
 AND COLUMNS.COLUMN_NAME = 'FAT_07_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.FAT_00_07
 ADD FAT_07_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [FAT_07_ID] de relação com o cabeçalho no novo [FAT_07_ID_OLD] bkp 
 UPDATE FAT_00_07
 SET    FAT_07_ID_OLD = FAT_07_ID
 WHERE  FAT_07_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_FAT_07_ID pra gerar os novos IDs do campo [FAT_07_ID] 
 UPDATE FAT_00_07
 SET    FAT_07_ID = NEXT VALUE FOR SEQ_FAT_07_ID
 GO 
  
 -- Recria índice Primary Key da table  [FAT_07_ID] para o campo [FAT_07_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'FAT_00_07'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [FAT_00_07]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'FAT_00_07_PK')
             ALTER TABLE dbo.FAT_00_07
             DROP CONSTRAINT FAT_00_07_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.FAT_00_07
