 -- Cria o SEQUENCE para o campo [AFC_ID] da table [AFC_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_AFC_ID'))
  CREATE SEQUENCE dbo.SEQ_AFC_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [AFC_ID_OLD] pra receber IDs do campo [AFC_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'AFC_00'
 AND COLUMNS.COLUMN_NAME = 'AFC_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.AFC_00
 ADD AFC_ID_OLD int NULL 
 END; 
 GO 
  
 -- Guarda [AFC_ID] de relação com o cabeçalho no novo [AFC_ID_OLD] bkp 
 UPDATE AFC_00
 SET    AFC_ID_OLD = AFC_ID
 WHERE  AFC_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_AFC_ID pra gerar os novos IDs do campo [AFC_ID] 
 UPDATE AFC_00
 SET    AFC_ID = NEXT VALUE FOR SEQ_AFC_ID
 GO 
  
 -- Recria índice Primary Key da table  [AFC_ID] para o campo [AFC_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'AFC_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [AFC_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'AFC_00_PK')
             ALTER TABLE dbo.AFC_00
             DROP CONSTRAINT AFC_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.AFC_00
 ADD CONSTRAINT AFC_00_pk 
 PRIMARY KEY CLUSTERED (AFC_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [AFC_00] 
 REBUILD 
 END
 GO