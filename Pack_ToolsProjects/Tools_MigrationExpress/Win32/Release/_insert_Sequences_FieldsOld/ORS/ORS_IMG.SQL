 -- Cria o SEQUENCE para o campo [ORS_ID_LOG] da table [ORS_01_LOGS]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_ORS_IMG_ID'))
  CREATE SEQUENCE dbo.SEQ_ORS_IMG_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- "1058" Criando campo [ORS_IMG_ID_OLD] pra receber IDs do campo [ORS_IMG_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'ORS_IMG'
 AND COLUMNS.COLUMN_NAME = 'ORS_IMG_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.ORS_IMG
 ADD ORS_IMG_ID_OLD int NULL 
 END; 
 GO 
  
 -- "1058" Criando campo [ORS_ID_OLD] pra receber IDs do campo [ORS_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'ORS_IMG'
 AND COLUMNS.COLUMN_NAME = 'ORS_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.ORS_IMG
 ADD ORS_ID_OLD int NULL 
 END; 
 GO

 -- "1267" Guarda [ORS_ID] de relação com o cabeçalho no novo [ORS_ID_OLD] bkp 
 UPDATE ORS_IMG
 SET    ORS_IMG_ID_OLD = ORS_IMG_ID,
 ORS_ID_OLD = ORS_ID
 WHERE  ORS_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_ORS_ID pra gerar os novos IDs do campo [ORS_ID] 
 UPDATE ORS_IMG
 SET    ORS_IMG_ID = NEXT VALUE FOR SEQ_ORS_IMG_ID
 GO 
  
 -- Recria índice Primary Key da table [ORS_IMG] para o campo [ORS_IMG_ID] 
