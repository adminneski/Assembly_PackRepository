 -- Cria o SEQUENCE para o campo [CRB_ID] da table [CRB_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CRB_ID'))
  CREATE SEQUENCE dbo.SEQ_CRB_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [CRB_ID_OLD] pra receber IDs do campo [CRB_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CRB_00'
 AND COLUMNS.COLUMN_NAME = 'CRB_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CRB_00
 ADD CRB_ID_OLD int NULL 
 END; 
 GO
 
 -- Guarda [CRB_ID] de relação com o cabeçalho no novo [CRB_ID_OLD] bkp 
 UPDATE CRB_00
 SET    CRB_ID_OLD = CRB_ID
 WHERE  CRB_ID_OLD IS NULL 
 GO 
  
 -- Criando campo [CRB_INTEGRACAO_OLD] pra receber BKP DAS INTEGRAÇÕES do campo [CRB_INTEGRACAO] 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'CRB_00'
             AND   COLUMNS.COLUMN_NAME = 'CRB_INTEGRACAO')
 BEGIN 
    IF NOT EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
                    WHERE COLUMNS.TABLE_NAME = 'CRB_00'
                    AND COLUMNS.COLUMN_NAME  = 'CRB_INTEGRACAO_OLD')
    BEGIN 
       ALTER TABLE dbo.CRB_00 ADD CRB_INTEGRACAO_OLD VARCHAR(50) NULL 
    END; 
 END; 
 GO 
  
 -- Guarda [CRB_INTEGRACAO] de integrações no novo [CRB_INTEGRACAO_OLD] bkp 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'CRB_00'
             AND   COLUMNS.COLUMN_NAME = 'CRB_INTEGRACAO')
 BEGIN 
    UPDATE CRB_00
    SET    [CRB_INTEGRACAO_OLD] = CRB_INTEGRACAO
    WHERE  [CRB_INTEGRACAO_OLD] IS NULL 
 END; 
 GO 
  
 -- Executa o SEQ_CRB_ID pra gerar os novos IDs do campo [CRB_ID] 
 UPDATE CRB_00
 SET    CRB_ID = NEXT VALUE FOR SEQ_CRB_ID
 GO 
  
 -- Recria índice Primary Key da table  [CRB_ID] para o campo [CRB_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CRB_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CRB_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CRB_00_PK')
             ALTER TABLE dbo.CRB_00
             DROP CONSTRAINT CRB_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CRB_00
 ADD CONSTRAINT CRB_00_pk 
 PRIMARY KEY CLUSTERED (CRB_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CRB_00] 
 REBUILD 
 END
 GO