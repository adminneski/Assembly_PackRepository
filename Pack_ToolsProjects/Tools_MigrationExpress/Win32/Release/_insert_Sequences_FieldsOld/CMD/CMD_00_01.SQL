 -- Cria o SEQUENCE para o campo [CMD_01_ID] da table [CMD_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CMD_01_ID'))
  CREATE SEQUENCE dbo.SEQ_CMD_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [CMD_01_ID_OLD] pra receber IDs do campo [CMD_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CMD_00_01'
 AND COLUMNS.COLUMN_NAME = 'CMD_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CMD_00_01
 ADD CMD_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [CMD_ID_OLD] pra receber IDs do campo [CMD_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CMD_00_01'
 AND COLUMNS.COLUMN_NAME = 'CMD_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CMD_00_01
 ADD CMD_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [CMD_01_ID] de relação com o cabeçalho no novo [CMD_01_ID_OLD] bkp 
 UPDATE CMD_00_01
 SET    CMD_01_ID_OLD = CMD_01_ID
 WHERE  CMD_01_ID_OLD IS NULL 
 GO 
  
 -- Guarda [CMD_ID] de relação com o cabeçalho no novo [CMD_ID_OLD] bkp 
 UPDATE CMD_00_01
 SET    CMD_ID_OLD = CMD_ID
 WHERE  CMD_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_CMD_01_ID pra gerar os novos IDs do campo [CMD_01_ID] 
 UPDATE CMD_00_01
 SET    CMD_01_ID = NEXT VALUE FOR SEQ_CMD_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [CMD_01_ID] para o campo [CMD_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CMD_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CMD_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CMD_00_01_PK')
             ALTER TABLE dbo.CMD_00_01
             DROP CONSTRAINT CMD_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CMD_00_01
 ADD CONSTRAINT CMD_00_01_pk 
 PRIMARY KEY CLUSTERED (CMD_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CMD_00_01] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [CMD_00] campo [CMD_ID], na table filha [CMD_00_01] campo [CMD_01_ID]
 UPDATE CMD_00_01 -- FILHA
 SET CMD_00_01.CMD_ID = T2.CMD_ID -- FILHA  RECEBE DA MÃE
 FROM CMD_00_01 -- FILHA
 INNER JOIN CMD_00 T2 ON (CMD_00_01.CMD_ID_OLD = T2.CMD_ID_OLD)
 AND (CMD_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
