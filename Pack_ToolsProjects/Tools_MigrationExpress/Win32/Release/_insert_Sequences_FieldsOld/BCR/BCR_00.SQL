 -- Cria o SEQUENCE para o campo [BCR_ID] da table [BCR_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_BCR_ID'))
  CREATE SEQUENCE dbo.SEQ_BCR_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [BCR_ID_OLD] pra receber IDs do campo [BCR_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'BCR_00'
 AND COLUMNS.COLUMN_NAME = 'BCR_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.BCR_00
 ADD BCR_ID_OLD int NULL 
 END; 
 GO 
  
 -- Guarda [BCR_ID] de relação com o cabeçalho no novo [BCR_ID_OLD] bkp 
 UPDATE BCR_00
 SET    BCR_ID_OLD = BCR_ID
 WHERE  BCR_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_BCR_ID pra gerar os novos IDs do campo [BCR_ID] 
 UPDATE BCR_00
 SET    BCR_ID = NEXT VALUE FOR SEQ_BCR_ID
 GO 
  
 -- Recria índice Primary Key da table  [BCR_ID] para o campo [BCR_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'BCR_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [BCR_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'BCR_00_PK')
             ALTER TABLE dbo.BCR_00
             DROP CONSTRAINT BCR_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.BCR_00
 ADD CONSTRAINT BCR_00_pk 
 PRIMARY KEY CLUSTERED (BCR_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [BCR_00] 
 REBUILD 
 END
 GO
