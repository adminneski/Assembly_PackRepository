 -- Cria o SEQUENCE para o campo [ETQ_01_ID] da table [ETQ_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_ETQ_01_ID'))
  CREATE SEQUENCE dbo.SEQ_ETQ_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [ETQ_01_ID_OLD] pra receber IDs do campo [ETQ_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'ETQ_00_01'
 AND COLUMNS.COLUMN_NAME = 'ETQ_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.ETQ_00_01
 ADD ETQ_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [ETQ_01_ID] de relação com o cabeçalho no novo [ETQ_01_ID_OLD] bkp 
 UPDATE ETQ_00_01
 SET    ETQ_01_ID_OLD = ETQ_01_ID
 WHERE  ETQ_01_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_ETQ_01_ID pra gerar os novos IDs do campo [ETQ_01_ID] 
 UPDATE ETQ_00_01
 SET    ETQ_01_ID = NEXT VALUE FOR SEQ_ETQ_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [ETQ_01_ID] para o campo [ETQ_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'ETQ_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [ETQ_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'ETQ_00_01_PK')
             ALTER TABLE dbo.ETQ_00_01
             DROP CONSTRAINT ETQ_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.ETQ_00_01
 ADD CONSTRAINT ETQ_00_01_pk 
 PRIMARY KEY CLUSTERED (ETQ_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [ETQ_00_01] 
 REBUILD 
 END
 GO