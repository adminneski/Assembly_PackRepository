 -- Cria o SEQUENCE para o campo [LIC_ID] da table [LIC_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_LIC_ID'))
  CREATE SEQUENCE dbo.SEQ_LIC_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
 -- Criando campo [LIC_ID_OLD] pra receber IDs do campo [LIC_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'LIC_00'
 AND COLUMNS.COLUMN_NAME = 'LIC_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.LIC_00
 ADD LIC_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [LIC_ID] de relação com o cabeçalho no novo [LIC_ID_OLD] bkp 
 UPDATE LIC_00
 SET    LIC_ID_OLD = LIC_ID
 WHERE  LIC_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_LIC_ID pra gerar os novos IDs do campo [LIC_ID] 
 UPDATE LIC_00
 SET    LIC_ID = NEXT VALUE FOR SEQ_LIC_ID
 GO 
  
 -- Recria índice Primary Key da table  [LIC_ID] para o campo [LIC_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'LIC_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [LIC_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'LIC_00_PK')
             ALTER TABLE dbo.LIC_00
             DROP CONSTRAINT LIC_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.LIC_00
 ADD CONSTRAINT LIC_00_pk 
 PRIMARY KEY CLUSTERED (LIC_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [LIC_00] 
 REBUILD 
 END
 GO