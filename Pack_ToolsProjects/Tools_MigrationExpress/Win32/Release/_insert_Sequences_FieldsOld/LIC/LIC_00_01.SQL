 -- Cria o SEQUENCE para o campo [LIC_01_ID] da table [LIC_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_LIC_01_ID'))
  CREATE SEQUENCE dbo.SEQ_LIC_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [LIC_01_ID_OLD] pra receber IDs do campo [LIC_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'LIC_00_01'
 AND COLUMNS.COLUMN_NAME = 'LIC_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.LIC_00_01
 ADD LIC_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [LIC_ID_OLD] pra receber IDs do campo [LIC_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'LIC_00_01'
 AND COLUMNS.COLUMN_NAME = 'LIC_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.LIC_00_01
 ADD LIC_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [LIC_01_ID] de relação com o cabeçalho no novo [LIC_01_ID_OLD] bkp 
 UPDATE LIC_00_01
 SET    LIC_01_ID_OLD = LIC_01_ID,
 LIC_ID_OLD = LIC_ID
 WHERE  LIC_01_ID_OLD IS NULL 
 GO 

 -- Executa o SEQ_LIC_01_ID pra gerar os novos IDs do campo [LIC_01_ID] 
 UPDATE LIC_00_01
 SET    LIC_01_ID = NEXT VALUE FOR SEQ_LIC_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [LIC_01_ID] para o campo [LIC_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'LIC_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [LIC_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'LIC_00_01_PK')
             ALTER TABLE dbo.LIC_00_01
             DROP CONSTRAINT LIC_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.LIC_00_01
 ADD CONSTRAINT LIC_00_01_pk 
 PRIMARY KEY CLUSTERED (LIC_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [LIC_00_01] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [LIC_00] campo [LIC_ID], na table filha [LIC_00_01] campo [LIC_01_ID]
 UPDATE LIC_00_01 -- FILHA
 SET LIC_00_01.LIC_ID = T2.LIC_ID -- FILHA  RECEBE DA MÃE
 FROM LIC_00_01 -- FILHA
 INNER JOIN LIC_00 T2 ON (LIC_00_01.LIC_ID_OLD = T2.LIC_ID_OLD)
 AND (LIC_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO