 -- Cria o SEQUENCE para o campo [FCM_ID] da table [FCM_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_FCM_ID'))
  CREATE SEQUENCE dbo.SEQ_FCM_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [FCM_ID_OLD] pra receber IDs do campo [FCM_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'FCM_00'
 AND COLUMNS.COLUMN_NAME = 'FCM_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.FCM_00
 ADD FCM_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [FCM_ID] de relação com o cabeçalho no novo [FCM_ID_OLD] bkp 
 UPDATE FCM_00
 SET    FCM_ID_OLD = FCM_ID
 WHERE  FCM_ID_OLD IS NULL 
 GO 
  
 -- Criando campo [FCM_INTEGRACAO_OLD] pra receber BKP DAS INTEGRAÇÕES do campo [FCM_INTEGRACAO] 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'FCM_00'
             AND   COLUMNS.COLUMN_NAME = 'FCM_INTEGRACAO')
 BEGIN 
    IF NOT EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
                    WHERE COLUMNS.TABLE_NAME = 'FCM_00'
                    AND COLUMNS.COLUMN_NAME  = 'FCM_INTEGRACAO_OLD')
    BEGIN 
       ALTER TABLE dbo.FCM_00 ADD FCM_INTEGRACAO_OLD VARCHAR(50) NULL 
    END; 
 END; 
 GO 
  
 -- Guarda [FCM_INTEGRACAO] de integrações no novo [FCM_INTEGRACAO_OLD] bkp 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'FCM_00'
             AND   COLUMNS.COLUMN_NAME = 'FCM_INTEGRACAO')
 BEGIN 
    UPDATE FCM_00
    SET    [FCM_INTEGRACAO_OLD] = FCM_INTEGRACAO
    WHERE  [FCM_INTEGRACAO_OLD] IS NULL 
 END; 
 GO 
  
 -- Executa o SEQ_FCM_ID pra gerar os novos IDs do campo [FCM_ID] 
 UPDATE FCM_00
 SET    FCM_ID = NEXT VALUE FOR SEQ_FCM_ID
 GO 
  
 -- Recria índice Primary Key da table  [FCM_ID] para o campo [FCM_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'FCM_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [FCM_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'FCM_00_PK')
             ALTER TABLE dbo.FCM_00
             DROP CONSTRAINT FCM_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.FCM_00
 ADD CONSTRAINT FCM_00_pk 
 PRIMARY KEY CLUSTERED (FCM_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [FCM_00] 
 REBUILD 
 END
 GO
  
