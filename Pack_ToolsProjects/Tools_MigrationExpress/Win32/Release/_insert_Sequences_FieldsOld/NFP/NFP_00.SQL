 -- Cria o SEQUENCE para o campo [NFP_ID] da table [NFP_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_NFP_ID'))
  CREATE SEQUENCE dbo.SEQ_NFP_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [NFP_ID_OLD] pra receber IDs do campo [NFP_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'NFP_00'
 AND COLUMNS.COLUMN_NAME = 'NFP_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.NFP_00
 ADD NFP_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [NFP_ID] de relação com o cabeçalho no novo [NFP_ID_OLD] bkp 
 UPDATE NFP_00
 SET    NFP_ID_OLD = NFP_ID
 WHERE  NFP_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_NFP_ID pra gerar os novos IDs do campo [NFP_ID] 
 UPDATE NFP_00
 SET    NFP_ID = NEXT VALUE FOR SEQ_NFP_ID
 GO 
  
 -- Recria índice Primary Key da table  [NFP_ID] para o campo [NFP_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'NFP_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [NFP_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'NFP_00_PK')
             ALTER TABLE dbo.NFP_00
             DROP CONSTRAINT NFP_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.NFP_00
 ADD CONSTRAINT NFP_00_pk 
 PRIMARY KEY CLUSTERED (NFP_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [NFP_00] 
 REBUILD 
 END
 GO
  
