 -- Cria o SEQUENCE para o campo [CEF_ID] da table [CEF_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CEF_ID'))
  CREATE SEQUENCE dbo.SEQ_CEF_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [CEF_ID_OLD] pra receber IDs do campo [CEF_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CEF_00'
 AND COLUMNS.COLUMN_NAME = 'CEF_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CEF_00
 ADD CEF_ID_OLD int NULL 
 END; 
 GO 

  
 -- Guarda [CEF_ID] de relação com o cabeçalho no novo [CEF_ID_OLD] bkp 
 UPDATE CEF_00
 SET    CEF_ID_OLD = CEF_ID
 WHERE  CEF_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_CEF_ID pra gerar os novos IDs do campo [CEF_ID] 
 UPDATE CEF_00
 SET    CEF_ID = NEXT VALUE FOR SEQ_CEF_ID
 GO 
  
 -- Recria índice Primary Key da table  [CEF_ID] para o campo [CEF_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CEF_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CEF_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CEF_00_PK')
             ALTER TABLE dbo.CEF_00
             DROP CONSTRAINT CEF_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CEF_00
 ADD CONSTRAINT CEF_00_pk 
 PRIMARY KEY CLUSTERED (CEF_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CEF_00] 
 REBUILD 
 END
 GO
  
