 -- Cria o SEQUENCE para o campo [DRQ_01_ID] da table [DRQ_00_01]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_DRQ_01_ID'))
  CREATE SEQUENCE dbo.SEQ_DRQ_01_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [DRQ_01_ID_OLD] pra receber IDs do campo [DRQ_01_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'DRQ_00_01'
 AND COLUMNS.COLUMN_NAME = 'DRQ_01_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.DRQ_00_01
 ADD DRQ_01_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo [DRQ_ID_OLD] pra receber IDs do campo [DRQ_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'DRQ_00_01'
 AND COLUMNS.COLUMN_NAME = 'DRQ_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.DRQ_00_01
 ADD DRQ_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [DRQ_01_ID] de relação com o cabeçalho no novo [DRQ_01_ID_OLD] bkp 
 UPDATE DRQ_00_01
 SET    DRQ_01_ID_OLD = DRQ_01_ID
 WHERE  DRQ_01_ID_OLD IS NULL 
 GO 
  
 -- Guarda [DRQ_ID] de relação com o cabeçalho no novo [DRQ_ID_OLD] bkp 
 UPDATE DRQ_00_01
 SET    DRQ_ID_OLD = DRQ_ID
 WHERE  DRQ_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_DRQ_01_ID pra gerar os novos IDs do campo [DRQ_01_ID] 
 UPDATE DRQ_00_01
 SET    DRQ_01_ID = NEXT VALUE FOR SEQ_DRQ_01_ID
 GO 
  
 -- Recria índice Primary Key da table  [DRQ_01_ID] para o campo [DRQ_01_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'DRQ_00_01'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [DRQ_00_01]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'DRQ_00_01_PK')
             ALTER TABLE dbo.DRQ_00_01
             DROP CONSTRAINT DRQ_00_01_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.DRQ_00_01
 ADD CONSTRAINT DRQ_00_01_pk 
 PRIMARY KEY CLUSTERED (DRQ_01_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [DRQ_00_01] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [DRQ_00] campo [DRQ_ID], na table filha [DRQ_00_01] campo [DRQ_01_ID]
 UPDATE DRQ_00_01 -- FILHA
 SET DRQ_00_01.DRQ_ID = T2.DRQ_ID -- FILHA  RECEBE DA MÃE
 FROM DRQ_00_01 -- FILHA
 INNER JOIN DRQ_00 T2 ON (DRQ_00_01.DRQ_ID_OLD = T2.DRQ_ID_OLD)
 AND (DRQ_00_01.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
