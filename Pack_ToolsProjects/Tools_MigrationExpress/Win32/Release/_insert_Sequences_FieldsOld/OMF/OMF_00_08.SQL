 -- "1070" Criando campo [OMF_ID_OLD] pra receber IDs do campo [OMF_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'OMF_00_08'
 AND COLUMNS.COLUMN_NAME = 'OMF_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.OMF_00_08
 ADD OMF_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [OMF_ID] de relação com o cabeçalho no novo [OMF_ID_OLD] bkp 
 UPDATE OMF_00_08
 SET    OMF_ID_OLD = OMF_ID
 WHERE  OMF_ID_OLD IS NULL 
 GO 
   
 -- Recria índice Primary Key da table  [] para o campo [] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'OMF_00_08'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [OMF_00_08]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'OMF_00_08_PK')
             ALTER TABLE dbo.OMF_00_08
             DROP CONSTRAINT OMF_00_08_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.OMF_00_08
 ADD CONSTRAINT OMF_00_08_pk 
 PRIMARY KEY CLUSTERED (OMF_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [OMF_00_08] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [OMF_00] campo [OMF_ID], na table filha [OMF_00_08] campo []
 UPDATE OMF_00_08 -- FILHA
 SET OMF_00_08.OMF_ID = T2.OMF_ID -- FILHA  RECEBE DA MÃE
 FROM OMF_00_08 -- FILHA
 INNER JOIN OMF_00 T2 ON (OMF_00_08.OMF_ID_OLD = T2.OMF_ID_OLD)
 AND (OMF_00_08.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO 
  
