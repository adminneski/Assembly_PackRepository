 -- Cria o SEQUENCE para o campo [CRE_05_ID] da table [CRE_00_05]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CRE_05_ID'))
  CREATE SEQUENCE dbo.SEQ_CRE_05_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [CRE_05_ID_OLD] pra receber IDs do campo [CRE_05_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CRE_00_05'
 AND COLUMNS.COLUMN_NAME = 'CRE_05_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CRE_00_05
 ADD CRE_05_ID_OLD int NULL 
 END 
 GO 
  
 -- Criando campo table MÂE [CRE_ID_OLD] pra receber IDs do campo [CRE_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CRE_00_05'
 AND COLUMNS.COLUMN_NAME = 'CRE_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CRE_00_05
 ADD CRE_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [CRE_05_ID] de relação com o cabeçalho no novo [CRE_05_ID_OLD] bkp 
 UPDATE CRE_00_05
 SET    CRE_05_ID_OLD = CRE_05_ID,
 CRE_ID_OLD = CRE_ID
 WHERE  CRE_05_ID_OLD IS NULL 
 GO
 
 -- Executa o SEQ_CRE_05_ID pra gerar os novos IDs do campo [CRE_05_ID] 
 UPDATE CRE_00_05
 SET    CRE_05_ID = NEXT VALUE FOR SEQ_CRE_05_ID
 GO 
  
 -- Recria índice Primary Key da table  [CRE_05_ID] para o campo [CRE_05_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CRE_00_05'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CRE_00_05]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CRE_00_05_PK')
             ALTER TABLE dbo.CRE_00_05
             DROP CONSTRAINT CRE_00_05_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CRE_00_05
 ADD CONSTRAINT CRE_00_05_pk 
 PRIMARY KEY CLUSTERED (CRE_05_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CRE_00_05] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [CRE_00] campo [CRE_ID], na table filha [CRE_00_05] campo [CRE_05_ID]
 UPDATE CRE_00_05 -- FILHA
 SET CRE_00_05.CRE_ID = T2.CRE_ID -- FILHA  RECEBE DA MÃE
 FROM CRE_00_05 -- FILHA
 INNER JOIN CRE_00 T2 ON (CRE_00_05.CRE_ID_OLD = T2.CRE_ID_OLD)
 AND (CRE_00_05.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO