 -- Cria o SEQUENCE para o campo [CRE_02_02_ID] da table [CRE_00_02_02]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CRE_02_02_ID'))
  CREATE SEQUENCE dbo.SEQ_CRE_02_02_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [CRE_ID_OLD] pra receber IDs do campo [CRE_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CRE_00_02_02'
 AND COLUMNS.COLUMN_NAME = 'CRE_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CRE_00_02_02
 ADD CRE_ID_OLD int NULL 
 END; 
 GO 
  
 -- Criando campo [CRE_02_02_ID_OLD] pra receber IDs do campo [CRE_02_02_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CRE_00_02_02'
 AND COLUMNS.COLUMN_NAME = 'CRE_02_02_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CRE_00_02_02
 ADD CRE_02_02_ID_OLD int NULL 
 END; 
 GO
  
 -- Guarda [CRE_ID_OLD] e [CRE_02_02_ID_OLD] de relação com o cabeçalho no novo field [_OLD] 
 UPDATE CRE_00_02_02
 SET CRE_ID_OLD = CRE_ID
 ,CRE_02_02_ID_OLD = CRE_02_02_ID
 WHERE CRE_ID_OLD IS NULL 
 GO 

-- "INTEGRACÃO" Criando campo [CRE_INTEGRACAO_OLD] pra receber BKP DAS INTEGRAÇÕES do campo [CRE_INTEGRACAO] 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'CRE_00_02_02'
             AND   COLUMNS.COLUMN_NAME = 'CRE_INTEGRACAO')
 BEGIN 
    IF NOT EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
                    WHERE COLUMNS.TABLE_NAME = 'CRE_00_02_02'
                    AND COLUMNS.COLUMN_NAME  = 'CRE_INTEGRACAO_OLD')
    BEGIN 
       ALTER TABLE dbo.CRE_00_02_02 ADD CRE_INTEGRACAO_OLD VARCHAR(50) NULL 
    END; 
 END; 
 GO 
  
 -- "INTEGRACÃO" Guarda [CRE_INTEGRACAO] de integrações no novo [CRE_INTEGRACAO_OLD] bkp 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'CRE_00_02_02'
             AND   COLUMNS.COLUMN_NAME = 'CRE_INTEGRACAO')
 BEGIN 
    UPDATE CRE_00_02_02
    SET    [CRE_INTEGRACAO_OLD] = CRE_INTEGRACAO
    WHERE  [CRE_INTEGRACAO_OLD] IS NULL 
 END; 
 GO 

 -- Executa o SEQ_CRE_ID pra gerar os novos IDs do campo [CRE_ID] 
 UPDATE CRE_00_02_02
 SET    CRE_02_02_ID = NEXT VALUE FOR SEQ_CRE_02_02_ID
 GO 
  
 -- Recria índice Primary Key da table  [CRE_02_02_ID] para o campo [CRE_02_02_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CRE_00_02_02'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CRE_00_02_02]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CRE_00_02_02_PK')
             ALTER TABLE dbo.CRE_00_02_02
             DROP CONSTRAINT CRE_00_02_02_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CRE_00_02_02
 ADD CONSTRAINT CRE_00_02_02_pk 
 PRIMARY KEY CLUSTERED (CRE_02_02_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CRE_00_02_02] 
 REBUILD 
 END
 GO
  
-- Atualiza novos ids da mãe [CRE_00] campo [CRE_ID], na table filha [CRE_00_02_02] campo [CRE_ID]
 UPDATE CRE_00_02_02 -- FILHA
 SET CRE_00_02_02.CRE_ID = T2.CRE_ID -- FILHA  RECEBE DA MÃE
 FROM CRE_00_02_02 -- FILHA
 INNER JOIN CRE_00 T2 ON (CRE_00_02_02.CRE_ID_OLD = T2.CRE_ID_OLD)
 AND (CRE_00_02_02.COM_CODIGO    = T2.COM_CODIGO)  --RELACIONA FILHA COM MAE
 GO