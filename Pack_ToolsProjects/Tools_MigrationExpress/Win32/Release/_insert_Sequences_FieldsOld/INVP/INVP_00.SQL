 -- Cria o SEQUENCE para o campo [INVP_ID] da table [INVP_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_INVP_ID'))
  CREATE SEQUENCE dbo.SEQ_INVP_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- Criando campo [INVP_ID_OLD] pra receber IDs do campo [INVP_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'INVP_00'
 AND COLUMNS.COLUMN_NAME = 'INVP_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.INVP_00
 ADD INVP_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [INVP_ID] de relação com o cabeçalho no novo [INVP_ID_OLD] bkp 
 UPDATE INVP_00
 SET    INVP_ID_OLD = INVP_ID
 WHERE  INVP_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_INVP_ID pra gerar os novos IDs do campo [INVP_ID] 
 UPDATE INVP_00
 SET    INVP_ID = NEXT VALUE FOR SEQ_INVP_ID
 GO 
  
 -- Recria índice Primary Key da table  [INVP_ID] para o campo [INVP_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'INVP_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [INVP_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'INVP_00_PK')
             ALTER TABLE dbo.INVP_00
             DROP CONSTRAINT INVP_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.INVP_00
 ADD CONSTRAINT INVP_00_pk 
 PRIMARY KEY CLUSTERED (INVP_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [INVP_00] 
 REBUILD 
 END
 GO

