 -- Cria o SEQUENCE para o campo [CIAP_ID] da table [CIAP_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_CIAP_ID'))
  CREATE SEQUENCE dbo.SEQ_CIAP_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [CIAP_ID_OLD] pra receber IDs do campo [CIAP_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'CIAP_00'
 AND COLUMNS.COLUMN_NAME = 'CIAP_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.CIAP_00
 ADD CIAP_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [CIAP_ID] de relação com o cabeçalho no novo [CIAP_ID_OLD] bkp 
 UPDATE CIAP_00
 SET    CIAP_ID_OLD = CIAP_ID
 WHERE  CIAP_ID_OLD IS NULL 
 GO
 
 -- Executa o SEQ_CIAP_ID pra gerar os novos IDs do campo [CIAP_ID] 
 UPDATE CIAP_00
 SET    CIAP_ID = NEXT VALUE FOR SEQ_CIAP_ID
 GO
  
 -- Recria índice Primary Key da table [CIAP_00] para o campo [CIAP_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'CIAP_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [CIAP_00]
 DISABLE
     
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'CIAP_00_pk')
             ALTER TABLE dbo.CIAP_00
             DROP CONSTRAINT CIAP_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.CIAP_00
 ADD CONSTRAINT CIAP_00_pk 
 PRIMARY KEY CLUSTERED (CIAP_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [CIAP_00] 
 REBUILD 
 END
 GO