 -- Cria o SEQUENCE para o campo [ALG_ID] da table [ALG_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_ALG_ID'))
  CREATE SEQUENCE dbo.SEQ_ALG_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [ALG_ID_OLD] pra receber IDs do campo [ALG_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF not EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'ALG_00'
 AND COLUMNS.COLUMN_NAME = 'ALG_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.ALG_00
 ADD ALG_ID_OLD int NULL 
 END; 
 GO 
  
 -- Guarda [ALG_ID] de relação com o cabeçalho no novo [ALG_ID_OLD] bkp 
 UPDATE ALG_00
 SET    ALG_ID_OLD = ALG_ID
 WHERE  ALG_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_ALG_ID pra gerar os novos IDs do campo [ALG_ID] 
 UPDATE ALG_00
 SET    ALG_ID = NEXT VALUE FOR SEQ_ALG_ID
 GO 
  
 -- Recria índice Primary Key da table  [ALG_ID] para o campo [ALG_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'ALG_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [ALG_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'ALG_00_PK')
             ALTER TABLE dbo.ALG_00
             DROP CONSTRAINT ALG_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.ALG_00
 ADD CONSTRAINT ALG_00_pk 
 PRIMARY KEY CLUSTERED (ALG_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [ALG_00] 
 REBUILD 
 END
 GO