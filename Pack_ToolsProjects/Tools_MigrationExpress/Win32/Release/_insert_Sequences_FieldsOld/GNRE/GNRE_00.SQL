 -- Cria o SEQUENCE para o campo [GNRE_ID] da table [GNRE_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_GNRE_ID'))
  CREATE SEQUENCE dbo.SEQ_GNRE_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO
  
 -- Criando campo [GNRE_ID_OLD] pra receber IDs do campo [GNRE_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'GNRE_00'
 AND COLUMNS.COLUMN_NAME = 'GNRE_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.GNRE_00
 ADD GNRE_ID_OLD int NULL 
 END 
 GO
  
 -- Guarda [GNRE_ID] de relação com o cabeçalho no novo [GNRE_ID_OLD] bkp 
 UPDATE GNRE_00
 SET    GNRE_ID_OLD = GNRE_ID
 WHERE  GNRE_ID_OLD IS NULL 
 GO 
  
 -- "INTEGRACÃO" Criando campo [GNRE_INTEGRACAO_OLD] pra receber BKP DAS INTEGRAÇÕES do campo [GNRE_INTEGRACAO] 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'GNRE_00'
             AND   COLUMNS.COLUMN_NAME = 'GNRE_INTEGRACAO')
 BEGIN 
    IF NOT EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
                    WHERE COLUMNS.TABLE_NAME = 'GNRE_00'
                    AND COLUMNS.COLUMN_NAME  = 'GNRE_INTEGRACAO_OLD')
    BEGIN 
       ALTER TABLE dbo.GNRE_00 ADD GNRE_INTEGRACAO_OLD VARCHAR(50) NULL 
    END; 
 END; 
 GO 
  
 -- "INTEGRACÃO" Guarda [GNRE_INTEGRACAO] de integrações no novo [GNRE_INTEGRACAO_OLD] bkp 
 IF EXISTS ( SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
             WHERE COLUMNS.TABLE_NAME  = 'GNRE_00'
             AND   COLUMNS.COLUMN_NAME = 'GNRE_INTEGRACAO')
 BEGIN 
    UPDATE GNRE_00
    SET    [GNRE_INTEGRACAO_OLD] = GNRE_INTEGRACAO
    WHERE  [GNRE_INTEGRACAO_OLD] IS NULL 
 END; 
 GO
  
 -- Executa o SEQ_GNRE_ID pra gerar os novos IDs do campo [GNRE_ID] 
 UPDATE GNRE_00
 SET    GNRE_ID = NEXT VALUE FOR SEQ_GNRE_ID
 GO 
  
 -- Recria índice Primary Key da table  [GNRE_ID] para o campo [GNRE_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'GNRE_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [GNRE_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'GNRE_00_PK')
             ALTER TABLE dbo.GNRE_00
             DROP CONSTRAINT GNRE_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.GNRE_00
 ADD CONSTRAINT GNRE_00_pk 
 PRIMARY KEY CLUSTERED (GNRE_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [GNRE_00] 
 REBUILD 
 END
 GO