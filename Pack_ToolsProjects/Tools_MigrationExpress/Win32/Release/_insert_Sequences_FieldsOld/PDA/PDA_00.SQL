             WHERE COLUMNS.TABLE_NAME  = 'PDA_00'
             AND   COLUMNS.COLUMN_NAME = 'PDA_INTEGRACAO')
 BEGIN 
    UPDATE PDA_00
    SET    [PDA_INTEGRACAO_OLD] = PDA_INTEGRACAO
    WHERE  [PDA_INTEGRACAO_OLD] IS NULL 
