 -- Cria o SEQUENCE para o campo [SCP_ID] da table [SCP_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_SCP_ID'))
  CREATE SEQUENCE dbo.SEQ_SCP_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
  
 -- "1058" Criando campo [SCP_ID_OLD] pra receber IDs do campo [SCP_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'SCP_00'
 AND COLUMNS.COLUMN_NAME = 'SCP_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.SCP_00
 ADD SCP_ID_OLD int NULL 
 END; 
 GO 
  
 -- "MOTHER" Guarda [SCP_ID] de relação com o cabeçalho no novo [SCP_ID_OLD] bkp 
 UPDATE SCP_00
 SET    SCP_ID_OLD = SCP_ID
 WHERE  SCP_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_SCP_ID pra gerar os novos IDs do campo [SCP_ID] 
 UPDATE SCP_00
 SET    SCP_ID = NEXT VALUE FOR SEQ_SCP_ID
 GO 
  
 -- Recria índice Primary Key da table  [SCP_ID] para o campo [SCP_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'SCP_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [SCP_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'PK_SCP_00')
             ALTER TABLE dbo.SCP_00
             DROP CONSTRAINT PK_SCP_00
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.SCP_00
 ADD CONSTRAINT SCP_00_pk 
 PRIMARY KEY CLUSTERED (SCP_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [SCP_00] 
 REBUILD 
 END
 GO
  
