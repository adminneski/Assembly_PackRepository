 -- Cria o SEQUENCE para o campo [DIZ_ID] da table [DIZ_00]
 IF NOT EXISTS( SELECT * FROM SYS.OBJECTS WHERE TYPE = 'SO'AND OBJECT_ID = OBJECT_ID(N'SEQ_DIZ_ID'))
  CREATE SEQUENCE dbo.SEQ_DIZ_ID
  AS int 
  START WITH 1
  NO MINVALUE 
  NO MAXVALUE 
  NO CYCLE 
  CACHE 
  GO 
  
 -- Criando campo [DIZ_ID_OLD] pra receber IDs do campo [DIZ_ID] PARA GUARDAR OS IDs ANTIGOS 
 IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.COLUMNS 
 WHERE COLUMNS.TABLE_NAME = 'DIZ_00'
 AND COLUMNS.COLUMN_NAME = 'DIZ_ID_OLD')
 BEGIN 
 ALTER TABLE dbo.DIZ_00
 ADD DIZ_ID_OLD int NULL 
 END 
 GO 
  
 -- Guarda [DIZ_ID] de relação com o cabeçalho no novo [DIZ_ID_OLD] bkp 
 UPDATE DIZ_00
 SET    DIZ_ID_OLD = DIZ_ID
 WHERE  DIZ_ID_OLD IS NULL 
 GO 
  
 -- Executa o SEQ_DIZ_ID pra gerar os novos IDs do campo [DIZ_ID] 
 UPDATE DIZ_00
 SET    DIZ_ID = NEXT VALUE FOR SEQ_DIZ_ID
 GO 
  
 -- Recria índice Primary Key da table  [DIZ_ID] para o campo [DIZ_ID] 
 IF EXISTS ( SELECT count(table_name) 
    FROM INFORMATION_SCHEMA.KEY_COLUMN_USAGE 
    WHERE table_name = 'DIZ_00'
    HAVING COUNT(*) > 1 ) 
 BEGIN
 -- DESABILITA INDICE 
 ALTER INDEX ALL ON [DIZ_00]
 DISABLE 
  
 -- VERIFICA SE EXISTE O INDICE E O APAGA 
 IF  EXISTS (SELECT * 
             FROM sys.indexes AS si 
             WHERE si.name = 'DIZ_00_PK')
             ALTER TABLE dbo.DIZ_00
             DROP CONSTRAINT DIZ_00_pk 
  
 -- ALTERA A TABLE CRIANDO O INDICE 
 ALTER TABLE dbo.DIZ_00
 ADD CONSTRAINT DIZ_00_pk 
 PRIMARY KEY CLUSTERED (DIZ_ID) 
 WITH ( 
   PAD_INDEX = OFF, 
   FILLFACTOR = 90, 
   IGNORE_DUP_KEY = OFF, 
   STATISTICS_NORECOMPUTE = OFF, 
   ALLOW_ROW_LOCKS = ON, 
   ALLOW_PAGE_LOCKS = ON) 
 ON [PRIMARY] 
  
 -- REABILITA INDICES 
 ALTER INDEX ALL ON [DIZ_00] 
 REBUILD 
 END
 GO
  
